        <h3>―― 池田さんはLPとして大変長いご経験をお持ちです。保全活動に対する考え方でキャリアや年月と共に変化した点などがありましたらお話をいただけませんか。</h3>

        <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol10_img01.jpg" alt="Special interview">
        <p><span class="text-blue">池田：</span>最初のころは、やはりお客様になっていただいた事、加入していただいた事に対する感謝の気持ちが強くあり、とにかく何かをして差し上げなければという思いで様々な取り組みをしていました。例えば、誕生日のメッセージカードを送るなどです。しかし、お客様の数が増えてくると対応が難しくなりました。お客様のために良かれと思って始めた取り組みも、ちょっとした間違いがかえって失礼に当たるケースもあります。例えば、年賀状を喪中の方に送ってしまうとか、住所が変わっていた事に気付かずに元の住所に送ってしまったケースもありました。こうしたミスが結構起こり得る事を考えるにつき、保全はやはり難しいものだと感じました。そこで悩んだ末に行き着いた結論は、「可能な限り全てのお客様と平等に接する」を最優先に考えるという事です。このお客様はたくさんのご紹介をいただけるからとか、大きな保険料や保障額で契約していただいているからなどで特別視するのではなく、全てのお客様に平等に関与すべきだとの思いに至りました。その後、徐々に現在の保全スタイルに変化していったと思います。</p>
        
      </section>
