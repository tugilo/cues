      <section class="interview-box">
        <h3>―― 池田さんが普段取り組んでいらっしゃる保全活動について簡単に教えていただけますか。</h3>
              
        <p><span class="text-blue">池田：</span>私の場合は、まず年5回通信物をお送りしています。また、「何月にどのお客様をフォローするか」を事前に決めていて、そのスケジュールに基づいて毎期ごとに動いています。</p>

        <p><span class="text-blue">聞き手：</span>池田さんは保全活動として、外部事業者様制作のニュースレターをお使いですが、確か春、夏、秋と季節ごとにお出しになり、年末は私どものカレンダーをお使いいただいて、年明けの1月もニュースレターを送っていらっしゃったかと記憶しております。カレンダーとあわせて年に5回、お客様に通信物をお送りしている、という事でしょうか？</p>

        <p><span class="text-blue">池田：</span>はい。そうです。一方で年賀状と暑中見舞いは、今は出していません。正確にお話しすると年賀状をやめたのは、一昨年の事です。それまでは年末にカレンダーを、年始には年賀状をお届けしていました。今は年賀状がニュースレターに変わった感じです。お客様には基本的に「年に5回」、私から何らかのアクションを取るように考えています。以前であればそれは年賀状や暑中見舞いのほか、「ご契約から〇年経ちました」などのお手紙だった訳ですが、今ではカレンダーとニュースレターをお届けする事で全てを置き換えました。ニュースレターが年4回と、年末のカレンダーで年に5回こちらからアクションを取れるようになりましたから。</p>

        <p><span class="text-blue">聞き手：</span>因みに、「年に5回」という方針は、どのような経緯でそのようにお考えなのでしょうか？</p>
        
        <p><span class="text-blue">池田：</span>それは、私が入社4、5年目位のころでしょうか。大手自動車メーカーのセールスマンの方と知り合った事がきっかけです。その方から、「お客様に年5回の自分の名前の入った通信物をお届けする事によって、年に1回、そのお客様と直接お会いするのと統計上同じ効果がある」というお話を伺いました。確か先方の社内教育の一環で社員にそう教えている、という事だったと思います。お客様に年5回何らかの通信物をお届けすれば、年に1度の実際の面談と同じ効果が得られるなら良いな、という事でその話を参考にそれ以来ずっと続けてきました。カレンダーやニュースレターを導入する前は、年5回何を送るか自分でいろいろ考え、試行錯誤しながらやっていました。</p>

        <p><span class="text-blue">聞き手：</span>お送りする通信物に関して何か特に心がけている事はございますか？</p>

        <p><span class="text-blue">池田：</span>私はその辺のセンスが無い（笑）ので、むしろカレンダーもニュースレターも色々とお任せできるのが有難いと思っています。少し楽をさせてもらっていますけども。私が何か題材を用意して発信するというより、予め準備していただいたものに私も便乗させてもらっている、という感じです。心がけている事としては、いつも制作をお願いしている事業者さんのニュースレターを改めて自分で読み込んで、お客様から何かお問い合わせがあったときにお応えできるように準備をしています。私自身も勉強になりますし、参考にさせてもらっています。</p>

        <p><span class="text-blue">聞き手：</span>コロナ禍があって、ライフプランナー(以降LP)様の活動が大きく制約される事もあったかと思います。その間にオンラインの面談など新しい活動スタイルが急速に普及しましたが、コロナ禍前と今とを比較して何か変わった事はありますか？</p>

        <p><span class="text-blue">池田：</span>はい。オンラインが普及し始めたころ、正直私はそれに否定的な見方をしていました。しかし、それはそれでコミュニケーションの手段のひとつとしてありだな、という認識に変わってきました。やはり、お客様が望まれるものを取り入れていくべきだろうと思っています。</p>

        <p><span class="text-blue">聞き手：</span>コロナ禍で活動が制約される半面、オンラインの普及で効率化された面もあると私は感じますが、オンラインは対面で面
談する場合との距離感の違いといいますか、戸惑われたご経験はありましたか？</p>
        
        <p><span class="text-blue">池田：</span>もちろんあります。直接お会いしないのはやはり不安があります。やはり空気感や温度差などというのが伝わりにくいと感じています。実際にお客様とオンラインでお話しても最終的に一度は対面で面談させていただく事が多いです。ちょっと語弊があるかもしれませんが、関連するお話として私たちLP側から能動的に保全のアクションを行おうとするスタンスでいこうとすれば、お客様の数やエリアの問題を含めて1人の担当者が全てのお客様に対して完全に、且つ平等にフォローアップするのは、今の会社の仕組みではなかなか難しいと考えています。ですから、例えば事前に予測できるような事象であれば、「何年ぐらい後にはこういう事が考えられるので、そのときはこういうフォローを行います」などとお伝えしますが、反対に予測がしにくい事象については、「お客様からのお申し出がない限りフォローはできません」といった話をご加入時にさせていただきます。保険のご契約は私の為の契約ではなく、お客様ご自身の契約です。お客様ご自身の責任で保険を利用していただく事が重要だと私は考えています。したがって、面談についても私のほうから常に積極的なアクションを起こすというスタンスは取りません。もちろん、お客様が必要とする時には必ず迅速に対応しますが、必要が無いのに積極的に私からアプローチをする事は基本的にありません。その点で、あくまで私の場合ですがオンラインのメリットとされる点が有益であるとはあまり思っていません。逆に、特段御用もないのにオンラインで繋いだところで何をお話するのか。もし表敬訪問やご挨拶が目的であれば必要性はあまり無いと思います。それならば、本当に通りすがりに名刺だけを置くことと同じではないでしょうか。お会いしなければならない用事があるときは、必ず、「今回はこういうご用件で少しお時間をいただけますか」とお伝えしてアポイントをいただきますから、用事なく面談する事はほぼないですね。</p>
        
      </section>
