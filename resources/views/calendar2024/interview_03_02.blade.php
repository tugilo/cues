          <h3>―― 十人十色であられるお客様は田内さんにどのような事を期待されていらっしゃると思われますか？</h3>

          <p><span class="text-blue">田内：</span>そうですね、個人の方は「必要な時には来て。そうじゃない時はそんなにしょっちゅう来られても困るよ」と、そんな風に思っているのかなと僕は勝手に想像しています。お客様には、それぞれちょうどよい距離感があると思います。「暇だし、話し相手に来てよ」と思う方もおられるし、「たまには面白い話聞かせてよ」と思っている方もおられます。一律ではなくいろいろだと思います。特に個人の方にはお申込の際に「私はしょっちゅう来る事はありません。でも、もしも非常事態のボタンを押してくれたら直ぐに伺います」と必ずお伝えします。本当に必要な時は直ぐに行きますという事です。それと、「将来、もしこんな事があったら連絡をくださいね」というお話はします。バースデーコールについても、ご契約の時にお話していて、「年に一度必ずお電話します」と。これも途中から言い始めた感じですね。最初の頃はそうでもなかったのですが、今はお申込の時に、今後のお付き合いの仕方でお伝えするようにしています。個人のお客様も、経営者でいらっしゃるお客様も同様です。<br>また、オーナー経営者の方には「法人の決算時期や新年度の4月とか、そういったタイミングでは訪問させてもらう事もあると思うので、ご連絡を差し上げます」とお伝えします。一方、例えば現在みたいに世情が混乱している時には、「ご様子を伺うため、お電話をする事もあるかも知れません」とお伝えしています。経営者の方とは普段どんなお付き合いをしているかというと、私はゴルフもお酒も行きません。基本お酒の付き合いはゼロです。お酒もせず、ゴルフもせず、普通の日々のお付き合いの中だけですが、それでも保全活動として次につながっていると思います。一番の保全は、一番初めの在り方。ご契約に至るまでのプロセスです。保険の内容と共に保険以外のいわば周辺の事がとても大切だと思っています。私の場合、保険の契約になる時は、必ず法人の決算書を見せていただいたり、これまでの経過を伺ったり、役員/社員退職金、社員さんの人事制度について質問させてもらったり、一見すると保険とは余り関係のない話をたくさんしてもらいます。そのお話の中で、なんとなく「こいつはちょっと違うな。出来る奴だな」と思ってもらえるように常々勉強をしているつもりです。やはりお客様から、「こいつは優秀な奴だな。役に立つな」と思ってもらえるようでなければと思っています。実際に「困った時には、まず田内に電話！」と思ってくれている経営者の方もおられます。本業の経営判断はもちろんご自身でされますが、例えば、「頑張っている社員を取締役に入れていいかどうか？」という相談をされる経営者の方もおられますし、「役員の給料をどれ位にするか」という事を相談してくださる経営者の方もおられます。単に保険屋さんという括りではなく、税理士でもなく、社労士でもなく、「ライフプランナー」として経営者の方のお役に立ちたいと思っています。POJ創業者の坂口さんが日本に根付かせたかった「ライフプランナー」という新しい職業。その私なりのLP像を考えると、自分が一番パフォーマンスを発揮出来るのは、経営者の方の近くだと思っているので、「歯に衣着せずに嫌なことも言ってくれるし、厳しい質問をちゃんとしてくれる人」という風に思ってもらえるような存在でありたいですね。</p>
        </section>
