        <section class="interview-box">
          <h3>―― 田内さんはライフプランナー（以降LP）として大変長いキャリアと実績をお持ちですが、保全に対する具体的な取り組み方は当初の時と、キャリアと共に経過した時と変化した部分はありますか？<br>また、基本的に変わっていないものがあれば教えてください。　</h3>

          <p><span class="text-blue">田内：</span>先ず、変化した事についてお話ししますと、私は2002年12月に入社して、社長杯に7回入って8回目に初めて落としてしまいました。それが直接のきっかけだったと言えるかもしれません。その時の営業成績が今までで一番悪かったのですけど、それまでははっきり言って「売りたい気持ち」ばかりでした。なのでお客様のところに保全を兼ねて行くとなっても、やっぱり鞄の中には設計書が入っていて、出す、出さないは別にしても心の中では「売りたい」という気持ちがものすごく強かったと思います。しかし、2009年度に社長杯を落としてから、売ることを止めました。言いかえると積極的な営業活動を控えてしまったのです。すると、逆にお客様から「これはどうしたらいいですか？」という質問が増えてきたりして、入社して8～9年目ぐらいから「とにかく売りたい」から、「役に立ちたい」という気持ちが大きくなってきました。その時から「お客様の心の声にちゃんと応えたいな」という気持ちで、敢えて(お客様に)嫌がられるような質問もするようになったし、判らない事をそのまま放ったらかしにする事もしない、本当にお客様が不安に思っている事、心配に思っている事に応えたいなという気持ちで、ちゃんと関わるようになったら営業成績も伸びて、気付けば紹介も広がっていました。<br>具体的にどんな事をしたかというと、先程お話ししたお誕生日ごとのお電話や、エアメールなどはじめていきました。まずはお電話で、「少しお話を聞きに行きましょうか？」みたいな感じでお声がけし、お客様の所へ「手ぶらで行く」感覚でお話を聞く。具体的な質問が出てくれば「次にもう一度お会いしましょうか？」みたいな感じで次に進む。基本的にはお会いして程よい「距離感を保つ」事が大切で、売る事は二の次みたいな関わり方ですね。そんな風に活動が変わってきたと思います。<br>キャリアの短いLPがよく失敗してしまう事例かも知れませんが、「保険は売れるけど、距離感が離れる」みたいな話があります。例えば、結婚した時に保険に入ってもらいました。その後、そのご夫婦に子どもさんが産まれたら最初に「保障を増やさないといけませんね」と、恐らくこちらは当たり前のようにお話すると思います。しかし、お客様の方は子どもが増えたイコール、「この子たちの教育資金は将来どの位必要なのかな？」とか、「行く学校によって幾ら位変わるのかな？」という事に興味があるわけです。にもかかわらず、いきなり (既契約者の方に) 「死んでしまったらどうするのですか？」とか言ってしまう。例えば、起業された方でもそうですし、結婚したとか子どもが産まれたという人もそうですが、LPとしては、まず寄り添って「楽しみが増えますね」とか、「リスクもあるけど、大きなチャレンジで楽しみですね」とか、そんな話が出来ると思います。しかし、お客様に寄り添うことを忘れて「そんな事であれば直ぐに保障を増やさないといけない」と言ってしまう。<img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 my-4 my-lg-2" src="/images/interview/vol03_img02.jpg" alt="Special interview">もちろん、それはそれで正しいのですが、距離感を保つというのは「死んだらどうします？」という事よりも、「将来楽しみですね、その夢を一緒に考えていくのはワクワクしますね。」と寄り添った上で、「家族とのそんな時間が大事で、そうした守りたい大切なものがあってこその保険だと思います。<br>でも、こんな時になんですが、保険も見直しておかれては如何でしょうか？」と、お話しするとお客様はおおよそお判りいただけると思います。そのような姿勢が次の紹介につながると思うし、それこそが保全だと思うのです。「保障を増やさないといけない」の一辺倒になるとお客様との距離が離れてしまうのではないかと思います。要は、売りに行かない。お客様が個人であろうと会社であろうと、お客様のサポーターとして近くにいる。LPはその名のごとく、人生プランの計画者として傍らにいる、ということが大切ではないかと思います。</p>
