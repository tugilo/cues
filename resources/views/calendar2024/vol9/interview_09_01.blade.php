      <section class="interview-box">
        <h3>―― 智重さんが普段取り組んでおられる保全活動について教えていただけますか。</h3>
              
        <p><span class="text-blue">山本：</span>いつもお願いしている外部事業者さんに制作いただいたニュースレターを年4回お送りする事。それと当社のEMSSのメール。大きくはこの2つでEMSSのメールは毎月1回、1日に配信します。昔はヘッダーの挨拶文を毎月書いていたのですが、今は会社がつくってくれたものをそのままシステムで流してもらっています。メールのコンテンツは、経営者とそうでない方、個人と法人というふうに分けてやっています。</p>

        <p><span class="text-blue">聞き手：</span>私はEMSSの内容をあまりよく存じ上げていないのですが、コンテンツがそれぞれ異なるものなのですか。具体的にはどんな内容でしょうか？</p>

        <p><span class="text-blue">山本：</span>そうですね。EMSSは経営者向けと個人向けは少し内容が違います。コラムが2つあって、金融、お金まわりの経済情報が基本的には1つ、もう1つは保険に関する内容です。会社が用意してくれているので、それを自動的に送っています。</p>

        <p><span class="text-blue">聞き手：</span>山本さんが制作されているニュースレターについては、私もよく存じ上げているつもりなのですが、ニュースレターの反応と、EMSSの反応は違うものですか。</p>
        
        <p><span class="text-blue">山本：</span>恐らく、メールはほとんど読まれていないかなと思います。ニュースレターについては読んでいただける方とそうでない方に分かれると思います。しかし、お届けしている事については多くの方が認識していただいていると思います。いずれにせよお客様のライフプランをお考えいただくきっかけや、保険にもつながるような内容が用意されているので、手に取って読んでくださったお客様が気になるときにご連絡をいただくというイメージです。私がお任せしているニュースレターサービスは、裏面に自身の事を掲載できるページがあるのですが、私はその部分もお任せしています。</p>

        <p><span class="text-blue">聞き手：</span>季節のご挨拶として、暑中見舞いや年賀状などは行っていますか？</p>

        <p><span class="text-blue">山本：</span>現在は行っていません。年賀状は、最初は非常に時間をかけて作っていました。自分の似顔絵を入れて、毎年「本年のテーマ」を決めて、「1年間そのテーマに基づいて活動します」という意思表示などをしていました。お客様からも良いリアクションをいただけたと思います。ただ、それも5年ほど前にやめました。</p>

        <p><span class="text-blue">聞き手：</span>ご入社以来、お客様とのコミュニケーションの為のツールを色々と工夫されておられますが、智重さんはどのような点をこだわっておられますか？また、それは時期ごとに変化はあったのでしょうか？</p>

        <p><span class="text-blue">山本：</span>私はプルデンシャルに入社した当初から、「通信物は始めたら継続していく」という強い思いがありました。カレンダーや年賀状など、費用や手間の観点からも長く続けられる方法を選ぶように心掛けていました。結果的に現在はニュースレター中心に切り替え、特に年賀状については、当初は非常に力を入れて作っていましたが、「自分の思いを伝えるだけで、どこか自己満足に近いのではないか？」と感じるようになりました。そこで、「お客様は何を求めているのだろうか？」と自問するようになり、担当者の考えだけでなく、金融や経済に関する情報が必要なのではないかと考えるようになりました。カレンダーについては、お客様全数にお送りする事は控えていますが、必要とされる方に引き続きお届けしています。</p>

      </section>
