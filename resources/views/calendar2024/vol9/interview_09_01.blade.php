      <section class="interview-box">
        <h3>―― ご契約後、お客様は智重さんにどのような関わりを期待されてらっしゃると思いますか？</h3>

        <p><span class="text-blue">山本：</span>それは、私が聞きたいくらいです。正直に申し上げて、「お客様が私に何を求められているか？」を本気で知りたいと思っています。もちろんこちらで想像して、こうかなと思って日々やっていますが。<br>
          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol09_img01.jpg" alt="Special interview"> 「期待されている事柄」についての答えになるかどうかは分かりませんが、私のマーケットは基本的に経営者の方です。経営者は会社をお持ちなので、決算があります。その際には必ず資料をお渡ししたり、メールで送るだけでなく、ご契約内容の確認などについては、直接お会いして行うように心掛けています。また、お客様が他社で加入している保険なども概要を一覧表にまとめて差し上げます。これをもとに、戦略的な観点からアドバイスを差し上げたりします。保険の加入状況の管理を精緻に行うところが、私の一番得意としているところです。</p>
        
        <p><span class="text-blue">聞き手：</span>それはお客様と、相当深い信頼関係を築かなければ難しい事ですよね。</p>
        
        <p><span class="text-blue">山本：</span>ご提案する前に、保険証券を全て見せてもらいます。決算書も同様です。定款もお預かりします。そして、法人のお客様にとって一番大切な事は、お問い合わせに対して常に素早い対応を心掛ける事かと思います。</p>
        
        <p><span class="text-blue">聞き手：</span>：法人のお客様は、智重さんがキャリアを重ねると共に増えてきたのでしょうか？それとも最初から積極的に取り組まれていたのでしょうか？</p>
        
        <p><span class="text-blue">山本：</span>入社後、私は3Wを150週達成しました。法人マーケットに行こうと考えたのはその後です。それで、経営者の方との出会いを求めてゴルフや夜の会食などの活動に注力し始めました。しかし、それは上手くいきませんでした。それこそ、ゴルフ場や飲食店などにずっと入り浸っているような状況でしたから、保全も行き届かずお客様が離れていきました。その後、反省し、年賀状を始めました。まずは反省の意を伝えたかったので。ただ、それでも契約後にお会いしていないお客様に直接ご連絡する勇気はありませんでした。<br>そこで年賀状やカレンダーを送り、アンケートを添え、アンケートをお返しいただいたお客様に訪問しました。その際には、「私が間違っていました」という文章を携えて、自分を諫めるような感じです。その後、2009年6月に私は心筋梗塞で入院してしまい、それまでの健康診断や人間ドックの結果は非常に良かったのですが、35歳になる年で心筋梗塞は珍しいとドクターから言われました。本来、心筋梗塞で助かった場合は3日ほどで退院できるのですが、私は2週間ほどさまざまな精密検査を受けました。その結果、はっきりした原因は分からないと言われました。ストレス以外の要因はないと。そこで、自問自答して考えた結果、「お客様には何もできていない」というストレスがあると気づきました。それ以降、積極的に取り組もうと思い、保全に力を入れました。しかし、先程もお話しましたが、ある時点でそれは自己満足ではないかと思い始めました。もっと専門性の高い情報を提供したり、お客様からのお申し出に素早く対応できる体制を整えたりする事が、より重要だと考えるようになりました。<br>シニアコンサルティングライフプランナーだった2013年11月に初めてアシスタントを雇いました。その理由はお客様のご契約に関わる部分の保全を最も重視すべきと考えたからです。何故ならば、お客様のご契約内容を巡って、機を捉えてアナウンスすべき重要な事柄があって、それの抜け落ちを防ぐ必要があるからです。また、クイックレスポンスが可能な環境を整備する事は、ライフプランナー(以降LP)としてお客様から常に求められている基本的な事ですから、これについては方針を変えずに継続しています。</p>

      </section>
