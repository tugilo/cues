          <img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 mb-4 mb-lg-2" src="/images/interview/vol09_img03.jpg" alt="Special interview">

          <p><span class="text-blue">聞き手：</span>ちょうどゴルフの話題が出たので、「ロゴ入りゴルフボール」のお話もお聞かせいただきたいのですが。</p>

          <p><span class="text-blue">山本：</span>分かりました。ゴルフに関する事ですね。</p>

          <p><span class="text-blue">聞き手：</span>ありがとうございます。この対談記事をご覧になって初めてお知りになる方もおられると思いますので、改めて経緯をお話しすると、昨年、神戸第二支社の鎌谷さんからお声掛けで智重さんをご紹介いただき、私どもがプルデンシャルの「ロゴ入りゴルフボール」の販売を引き継がせていただく事になりました。こちら「ロゴ入りゴルフボール」ですが、智重さんが関わってらっしゃるのは、保全に活用できるツールだからといったお考えがあるからですね。</p>

          <p><span class="text-blue">山本：</span>：経緯をお話しすると、最初は恐らく2005年だったと思います。神戸でMDRT日本会があったとき、私は支部長だったのですが、理事をされていた（中村）マサさん、中須賀さん、鎌谷さん、（川崎）太郎さんのゴルフに混ぜていただいた事がありました。その際、「関西にもゴルフ部をつくろう」みたいな話になり、鎌谷さんが、「ゴルフ部だと堅苦しいし、支社の垣根を越えた交流が目的なので、『同好会』のほうがいいのでは？」となって、その事務局を私が任される事になりました。当時、その中に同好会メンバーだった高松支社のライフプランナーがいて、彼が担当するお客様にゴルフボールにロゴ入れを行う企業様がおられました。かねてより、高松支社では「ロゴ入りボール」を作ろうという話になっていたらしく、その話を振ってくださる形で、「関西ゴルフ同好会でもどうですか？」と私に打診がありました。それで、私、事務局担当として同好会のメンバーに発信したら、多くの方が「欲しい」と。その後、関東のゴルフ部にも広がり、今では全国に拡大しているという事ですね。それ以来、事務局担当として私が毎年希望数を取りまとめるようになりました。しかし、当初は1,000ダースほどでしたが、それが2,000ダースになり、3,000ダースになって・・・。当然、事務的にかなり負担となってきたので、誰かに引継ぎをしなければと思うようになりました。希望を募ると4名の方が手を挙げてくださったのですが、皆アシスタントが居ない方で、多分無理だろうなと思って引継ぎが出来ませんでした。それで、ご紹介で井手上さんにお会いし、お願いさせていただいたという流れです。</p>

          <p><span class="text-blue">聞き手：</span>関西ゴルフ同好会の方々を中心にご購入いただいていると思いますが、それにしても多くのLPの皆様がゴルフをされていることに驚きました。お客様にノベルティとしてお配りする、コンペの協賛品として購入されるLP様もおられると想像しています。</p>
          
          <p><span class="text-blue">山本：</span>そうですね。保全活動とか新規契約につながるようなコミュニケーションのツールになるのであれば、とても良い事かなと思っています。私のケースですが、例えば年末のご挨拶に訪問する際に、「お使いください。」と言ってご契約者様にお渡しすると、それを次回ゴルフでご一緒するときに使って下さるのです。それで、次第にお客様は皆、ロゴ入りボールをお使いいただけるようになります。そうすると、まだご契約者様ではない方が、「なに、それ？」とご興味を持たれるのです。どうやら、欲しいと思っていただける事が多いようで、「私も保険に入ったらもらえるのかな？」と言ってくださいます。もちろん、それは半ば冗談のような話ですから、私からは、「もちろん保険のご契約がなくてもお渡しできますのでどうぞ。お使いください。」という感じでお渡ししています。私の場合、一番多いのはコンペの協賛品としてお渡しするケースですね。私が参加するコンペは、「どうぞ使ってください。」と言って差し上げています。そうすると主催者の方には大変喜ばれますね。会費を集めてコンペ賞品を揃えるのとは別に、協賛品があるとそれも賞品として出せますからね。いつもとても喜んでいただけます。私はその使い方が一番は良いかなと思っています。話が脱線しますが、ロストボールを練習用ボールにしているゴルフ場も結構ありますよね。そこで、このボールがたまに出てきますよ。これ、結構嬉しく思う瞬間です。私もOBする事はありますが、皆さんがたくさんOBなさってロストボールになっても、結果的に多くの方に対して会社の宣伝になっていますから。これも、プルデンシャル生命全体の宣伝になるからいいかな、と考えています。</p>

          <p><span class="text-blue">聞き手：</span>ゴルフを愛好される方は、どちらかというと経営者の方や富裕層の方が多いですものね。考えてみれば、広くそういう方々の目に留まる効果は大きいですね。</p>

          <p><span class="text-blue">山本：</span>はい。ところで以前の私は、「ゴルフは見込み客発見の手段」であると捉えていました。しかし、今では全く逆で、「保全の為にゴルフをする」というイメージです。仮に、「ゴルフは見込み客発見の手段」の観点で、ゴルフで出会ったお客様に保険に加入していただいたら、その後もずっとゴルフに参加し続けなければならなくなります。しかし、お客様が増えてくると本当に体が足りなくなります。それで行かなくなると、「保険に入った途端に付き合いが悪くなった。」というご不満に発展する恐れがあります。したがって、今ではそこからの脱却を図っているところです。具体的には、先にゴルフではなくて、先に保険加入をいただいた方とゴルフに行くようにしています。今はそういう心掛けをしています。単純に、「この人、保険入ってくれるのと違うかな？」と思いながらゴルフをするのが嫌になったことも大きいです。ゴルフをもっともっと楽しみたいですからね。むしろ、そういう「見込み客発見の場」的なスタンスを取らないほうがご契約に繋がる事が多いです。ロータリークラブの活動もそうですが、「この人、保険に入ってくれるのと違うかな？」と思って接していても何も起こりませんが、一生懸命、会の為に献身的に活動したりすると自然と周りから声が掛かるのと同じイメージです。今は、そういった「待ちの営業」と、セミナー主催を柱とした「攻めの営業」と、両輪でやっているという感じです。ゴルフは、今では保全の手段です。</p>
          
          <p><span class="text-blue">聞き手：</span>：そうなのですね。やはり追わんとすればお客様は逃げてしまう、ということでしょうか。智重さん、本日は長時間にわたってお話をお聞かせいただき、誠にありがとうございました。今後とも引き続きよろしくお願いいたします。</p>

