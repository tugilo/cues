        <section class="interview-box">
          <h3>―― 私どものようにコミュニケーションツールの制作サイドに対して、今後の期待値やご要望があればお聞かせください。</h3>
          
          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol09_img02.jpg" alt="Special interview">

          <p><span class="text-blue">山本：</span>そうですね。キューズプラスさんのカレンダー、とても良いと思いますよ。こだわりを感じます。来年の写真はニューヨークですね。購入数を増やしてみようかな。制作物に関する要望があるとすれば、例えばお礼状のはがき等を検討していただければと。例えば写真撮影がセットになったはがき制作サービスとか、そういったものがあれば面白いと思います。あと、別途費用がかかっても良いので、カレンダーの送付状に例えば二次元バーコードを掲載して、そこから飛ぶと動画で、「今年一年お世話になりました。今年一年はこんな感じでした。来年もよろしくお願いします！」とLPが挨拶する映像が流れる、なんてどうでしょう。LPが実際に喋っている動画のリンクが一緒にカレンダーと一緒に届くサービスが出来るともっと良いと思います。もちろん、LPごとの動画撮影や編集もパッケージにしていただいて。</p>
          
          <p><span class="text-blue">聞き手：</span>ところで、余談にはなりますが、プルデンシャル生命の関西ゴルフ同好会さんとの繋がりがなかったら、恐らく私どものカレンダーはここまで全国的に受け入れていただけなかったと考えています。そういう意味で、智重さんがおられなければ、今の私は無いかも知れません。</p>

          <p><span class="text-blue">山本：</span>いえいえ。私自身、カレンダーでは貢献できている状況では無いですよ。実は私が本当にこの取材を受けて良いのかな、と思っていました(笑)。</p>
          
