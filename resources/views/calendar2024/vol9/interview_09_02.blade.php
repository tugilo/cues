          <h3>―― 次に、「保全活動を継続していくコツ」のようなものがあれば教えてください。</h3>
              
          <p><span class="text-blue">山本：</span>敢えて言うならば、コツは、多分どの人も同じ事を言うと思いますが、長く続けられる事だけをやる。それ以上でもそれ以下でもないでしょう。例えば、3年後位に、もしニュースレターを自分で書けるようになっているなら書きたいと思っています。しかし、今はそれよりも、そこは外部に制作を任せて出していこうという感じです。書けるようになってから始める、のではなくて、今出来る事をやればいいかなと。</p>

          <p><span class="text-blue">聞き手：</span>次に、保全活動全般に関してなのですが、LP様のなかには、「新規の活動が忙しくて、保全になかなか時間が割けない」というお話を伺う事があります。もし智重さんが後輩の方にこのような相談をされた場合、どのようなアドバイスをされますか？</p>

          <p><span class="text-blue">山本：</span>「もし自分が契約者やお客様だったら、何を求めるか」を考えるのが大切ですね。自分が契約者の立場ならこんな事を望むよな、と想像する。そしてそれに対してできる事柄を真面目にやる、という事です。例えば私の場合、自分の知識や経験値にあわせて、より高度な経済や金融の情報を自分の言葉でお客様にお伝えできるようになる為に、欠かさず勉強を続けています。やはり、お客様が望む事をまず想像する事が大切ではないでしょうか。その結果、もし学ぶ必要があるなら自分で学んだら良いし、難しいならアウトソーシングしても良い。私がアドバイスするのであれば、「常に相手の立場に立って、自分がお客様だったら何を望むか、何をしてほしいかを想像するように」と言うでしょう。決して自己満足にはならない。相手が何を求めているかを想像し、それに対して自分ができる事を行うのが大切だと思います。保全というのは Too Muchはない。やって多すぎるというのはなくて、やればやるほど良いものだと思います。しかし、例えば、最初にToo Muchに保全していたのに、だんだんとなくなっていくのは良くない。長い間ずっと何かを続けてくれるね、というほうがいいと思います。</p>

          <p><span class="text-blue">聞き手：</span>ありがとうございます。次に、現在キャリアの浅いLPの皆様向けにお話しするとして、智重さんにとって「保全活動とは？」を一言で表現するなら、如何でしょうか？</p>
          
          <p><span class="text-blue">山本：</span>「お客様との距離感を適正化する活動」であると思います。ご契約をお預かりする際は、何度もお会いする機会がありますよね。その状況はお客様との関係はホットな状態だと言えるでしょう。しかし、お話したように、かつての私は夜の会食とかゴルフばかりやっていたので、お客様との関係がコールドになってしまった訳です。私が伝えたいのは、ホットではなくなったお客様をまた再びホットな状態に戻すのは、ここでは重要ではないという事です。それよりも、できればウォームな関係をずっと長く保ち続ける事が大切ではないかと。そうすれば、何かあった時に私を思い出していただけるでしょう。そうして思い出してもらえたら、またそこでホットになるとタイミングもあると思います。そしてそれは、こちらからのアクションだけでは知り得ない事柄がたくさん含まれていると思います。だからこそ、距離を最適化する事は、お客様が何か相談したいと感じたときに思い出してもらう為に絶対に必要な活動かなと思います。何か相談ごとがある時、例えば保険以外でもお金まわりの事柄や住宅の購入など、何でも良いのですが、「智重さんに聞いたら誰か良い人を紹介してもらえるじゃないのかな？」などと思っていただけるように。その為にウォームな関係をずっと維持していく必要があると考えています。　</p>
        </section>
