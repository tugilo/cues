        <section class="interview-box">
          <h3>―― 今後CX(顧客体験)を重要視していくというお話を耳にしました。金指さんにとって「理想的な顧客体験」というのは、どのようにお考えになりますか？</h3>

          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol08_img02.jpg" alt="Special interview">

          <p><span class="text-blue">金指：</span>理想的な顧客体験とは…ですか。そうですね。<br>
          最近の話ですが、あるご夫婦のお客様で、ご主人様の勧めで奥様の保障をお預かりした事がありました。ご主人様は、私が新人の頃にご契約頂いて15年来のお客様ですが、奥様にお会いしたのは新婚当時の７年程前で、当時、奥様は他の保険会社に加入されていてそれほど見直しが必要な内容ではなかったので、そのまま継続されていました。でも、奥様の担当者が知らない間に辞めてしまっていたらしく、とても不安な気持ちになってしまったと。そうしたらご主人様が、「金指さんは15年ずっと担当でいてくれているし、何かあれば話を聞いてくれるから、金指さんが担当だと安心だよ」みたいにお話をしてくださったそうです。やはり、「何かあった時に、いつでも連絡して相談出来る人がいる」というのが重要で、そういう環境を整えて差し上げる事が大切だと思っています。</p>

          <p><span class="text-blue">聞き手：</span>私どもはもう4年、インタビューをさせて頂いているのですが、皆様口をそろえて、「すぐに相談が出来る、適切な距離感」のお話に言及される印象があります。お立場やキャリアを超えて、この事は極めてシンプルで、普遍的な考え方なのかもしれませんね。</p>

          <p><span class="text-blue">金指：</span>そうですね。</p>

          <p><span class="text-blue">聞き手：</span>コロナ禍を機に、メッセージアプリとかオンライン会議ツールなど、お客様とデジタルでつながる手段が多様化しています。そんな中、年賀状や私共のカレンダーなど、アナログな紙媒体をお客様にお送りする価値について、金指さんはどのようにお考えでしょうか？</p>

          <p><span class="text-blue">金指：</span>まず、私自身がアナログ人間でして(苦笑)。だから、紙に自筆で書く事が好きなんですね。年賀状は必ず一言添えるとことを大切にしていますし、御社のアンケートハガキもお客様が直筆で書いて頂いたものが返送されるので、お客様の温かみを感じられる事が何より価値だと思います。<br>
          少し話が逸れますが、保険金お届けの用件で、受取人であるお母様に保険金をお届けに伺った際に、お客様が書いた申込書控えの書面が出てきました。その時、お母様が、「申込書の字はあの子の字だから、そのまま残しておきたい…」とお話をされたのです。私、ものすごく感極まりましてね。彼女がどういう経緯で、どのような気持ちで保険に加入されたのかは私からもお伝えはしましたが、手書きはさらに想いが伝わると感じました。今なら電子署名のEAJがあって、これを使うと手続き上の不備が起きにくいので便利なのですが、同じようなケースがあった場合、ご家族にご本人のサインは残りません。私は紙で残るもの、直筆のものには特別な価値があると思っています。</p>

          <p><span class="text-blue">聞き手：</span>直筆の価値って、忘れられがちですよね。普段何気なく書くようなものが、ご契約とかそういう大切な節目で残るものと考えると、非常に重いですね。年賀状一枚一枚に直筆でお書きになるのは、そのようなご経験もあっての事でしょうか？</p>

          <p><span class="text-blue">金指：</span>その経験があるからというわけではないのですが、私は年賀状を自身で何も書かないで出すのは味気ないと思っていて、写真が有ったりするのなら判るのですが、例えば一般的なイラストだけで自筆が無い年賀状って、果たして何のために送っているのかな…？と思ってしまうのです。せっかく送るのですから、お一人お一人にお届けする、お送りする意味をしっかりお伝えしたいと思っています。<br>
          また、証券郵送の手続きの際、会社所定の定型フォームがあるんですね。支社長からのご挨拶文みたいなものなのですが、そこにも必ず一言手書きメッセージを入れて送っています。私はそれが普通だと思っていたのですが、ある日、「自筆のメッセージ入りは金指さんだけだよ」と以前の支社長から聞かされたことがあり、書いてる人って意外と少ないんだなと思いました。先ほどの保険金お届けの件というよりも私の日頃からの習慣というか、それが当たり前だと思うからですね。</p>
        </section>
