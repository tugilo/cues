          <h3>―― 「保全活動を継続していくコツ」のようなものがあれば、教えていただけますか？</h3>

          <p><span class="text-blue">金指：</span>私は保全活動らしい事はそんなにできていないと思います。だから、せめて年賀状とカレンダーだけは送らないと…、という感じです。途中で止めてしまうのが一番良くないと思っているので、自分が続けている事、始めた事は続ける。反対に続けられそうもないものには手を付けない、という感じです。コツというほどでもないかもしれませんが。</p>
        </section>
