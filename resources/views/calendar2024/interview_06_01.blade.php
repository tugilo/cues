        <h3>―― 井芹さんが普段取り組んでおられる保全活動について教えてください。</h3>

        <p><span class="text-blue">井芹：</span>毎月DMを送っています。ただ、忙しい時は2ヶ月に1回になる事もあります。DMを始めようと思ったのは、常に私たちは新規契約を追い掛けている為、ご契約をお預かりしているお客様には度々お会いしに行く事はしない事から「既契約者様にどんなフォローが出来るか？」と考えていました。その中でDMを送りたいというのはずっと頭にあって、当初は御社のカレンダーのようなツールがなかったのもあり、先ず自分で考えて何か作ろうと思いました。でも、いざやってみるとコンプライアンスチェックも必要ですし、内容を考えるのも一苦労(苦笑)。どうしたものかと思い、初めは春夏秋冬で季節のお葉書を送っていました。全員に手書きの文章を作ってお送りする事は難しかったので、宛名だけ必ず手書きにしていました。私は字が綺麗と言って頂けるので、それを活かそうと思って。他にはバースデーカードもずっと送っていました。それ位しか出来ていなかったのですが2007～08年頃にEMSSが導入されたので、これは良いと思いましたね。</p>

        <p><span class="text-blue">聞き手：</span>EMSSはメールで既契約者にお送りするシステムですね？</p>

        <p><span class="text-blue">井芹：</span>そうです。そこで閃いて(笑)。これを印刷出来ないのか、メールマガジン(配信)では無く郵送で送るので何か別のタイトルを付けられないかと本社に確認しました。そしたら印刷も出来、タイトルは任意で良いとの事だったので、ちょっとくだけた感じにしようと思い、「なおっぺ通信」というタイトルでDMを送るようにしました。紙面のテーマは、健康と雑学と保険という3つに分かれ、売り込み感は控え、各コラムの順番も健康、雑学、保険の順で3つを綴じてお出ししています。最近では私の写真も載せています。ご年配の方の中には、健康コラムをとても喜んで下さり、保存して下さっている方もいます。最初のうちは「今月来てないよ」「まだかな」という電話があった事もあり、結構楽しみにして下さっていましたが、10年ぐらい継続していると「当たり前」「また来ているな」という感じになりました(笑)。気を付けている点はコンテンツのボリュームですね。毎回、結構ボリュームがあって。その辺りは73歳の私の母を基準にしています。DMは実家にも必ず送るので、母の感想を参考にしています。例えば内容が多いよとか、字が小さいよとか(笑)。私のDMは読者対象をご年配の方向けに考えているので、母が読めているのであれば、「このぐらいのボリュームと、この字の大きさであれば大丈夫かな」と思って送らせて頂いています。若い方には全く問題ないとは思われるのですが(笑)。DMの効果は毎月に近い形でお送りしているので、「私の顔を知らない」「私の顔を覚えていない」という方はいらっしゃいません。引継ぎのお客様と初めてお会いする時、例えばカフェやファミレスで待ち合わせたりする時には、お客様が気づいて下さるので「誰だろう？どこだろう？」という事もありません。担当者の顔をお知らせするという事は、一度もお会いした事が無い引継ぎ担当のお客様にも有効だと思いますね。こちら(DM)を毎月送るようになってからはバースデーカードはやめました。でもバースデーコールはしていますよ。最近はお電話の他、メッセージを活用する事も多いのですが、どのような形であれ、バースデーコールは継続しています。<br>話が前後しますが担当者が変わった引継ぎのお客様は、「新しく担当になったのでお会いしたい」とこちらからご連絡しても「いや、いいです」と言われる方が結構多いんです。そうなると結局は「担当が誰か判らない」「担当者の顔が判らない」という事になってしまいます。会社にご連絡を頂いた際、「担当者が判らない」と言われた場合、クレームの一つとして取り扱われる事になりますし、また、自分が担当させていただいている以上は「誰が担当しているか判らない」という事は無いようにしなければなりません。DMの中には私の携帯番号も大きく入れたりして、マメに色々な事を散りばめています。心掛けている点は、続けられないと意味がないので私がやり易い事にこだわります。会社の中でも何人かで一緒にメールマガジンを作るチームの話も聞きますが、先ずは自分でコントロール出来るものにしたかったので、自身(個人)一人で出来るものという事ですね。そして将来、私が辞める事も有り得無くは無いので、退職になるのか死んでしまうのか判らないのですが(笑)。</p>

        <p><span class="text-blue">聞き手：</span>それは大変困ります！</p>

        <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol06_img01.jpg" alt="Special interview">

        <p><span class="text-blue">井芹：</span>私が会社を去った後、お客様を引継いでくれるライフプランナー(以降LP)が、「やろうと思えばやれなくはないもの」という事もポイントだと思うんです。素材は元々会社にあるツールですから。例えばお客様からご要望が有った場合、会社が毎月準備してくれているものであれば届けられるし、続けられます。負担なく引継ぎも出来るという事も保全活動を考える上で必要かなと思っています。「あの人はやってくれたのに、この人はやってくれない」となると、会社としてそれはどうなのかなと見られるかも知れません。会社にはスタンダードサービスというものがありますが、それ以外の保全は独自で考えたものが多く、私がやっている事よりも素敵で効果的にお客様に届けば、それはお客様にとって良い事と思いますが、逆に私もそうですが「前の人はやってくれていたのに〇〇さんはやってくれない」と言われるのは嫌ですよね。改めて色々考えた時に、何かしら出来るもの、手間が一番かからずに確実に定期的にやれるものとしてEMSSはすごく使えると思って使い始めました。私はコピペして印刷するだけだし、挨拶文もメールに載っているものをコピペしているだけで自身は考えてなくてもいい。それにお客様へ毎月お送りする事で、住所が判らないという方はほとんどいなくなります。届かなければ返ってきますから。郵便の場合は(転居先まで)追い掛けて転送されるので問題ない。でも、送ったものが返ってくる方は「何かあったな」「引っ越したのかも知れない」等、早くキャッチ出来ます。ご契約の連絡先とDMの送付先のご希望が違う方もいらっしゃるので、DM送付先の住所は気に留めておく必要があります。例えば、「証券は自宅に送って下さい。でも、私はマンション・アパートに住んでいます」という一人暮らしの方の場合などですね。他はご夫婦でどちらかお一方しかご契約を預かれない場合や、引継ぎの際に既にご結婚されている場合等、情報になるべく注意して、ご夫婦であれば必ずご夫婦の名前を入れるようにします。<br>通信物で言うと、年賀状も毎年出していますね。必ず裏に手書きで一言メッセージを入れています。私は個人の世帯でお預かりする事が多いので、お客様とは家族ぐるみで付き合っているような方も結構いらっしゃいます。そうすると自宅に喪中のはがきをいただいたりする場合も少なくなく、その時は年賀状をお出し出来ないので、代わりにクリスマスカードをお出しするようにしています。<br>そして御社のカレンダーです。カレンダーにも自分で作った挨拶レターを必ず入れます。その時には、この1年を通じた私自身の事をご報告するというスタンスです。そこで私と言う人物を知って貰いたいので、感じた事とか思った事をなるべく盛り込もうと心掛けています。<br>お届け以外は定期訪問や定期電話です。これは2、3年に一度必ず実施しています。そして、セミナー。「座談会」と言った方が良いかも知れません。不定期開催をしていたのですが、今年からは定期的にやろうと決めました。そのご案内もしています。個別でのご要望があった方には個別で柔軟に対応しています。これまで様々に開催してきましたが、ボリュームが多すぎると頭には残らないですね。テーマを絞って1時間をしっかりやる、という事を小まめに行った方が効果的かと思います。これは保全にもなるし、また紹介にも繋がると思います。</p>
