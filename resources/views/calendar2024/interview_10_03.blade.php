            <section class="interview-box">
              <h3>―― 現在キャリアの浅いLPの皆様向けにお話しするとして、「保全活動とは？」を一言で表現するなら、如何でしょうか？</h3>

              <p><span class="text-blue">池田：</span>：先ほどもお話しした内容ですが、保全活動とは、「お客様との距離感を縮める作業」であると思っています。まさに、それに尽きます。もちろん「新規の為の活動」に繋がります。全てはそこから始まると言っても良いでしょう。新規の活動とは、新しいお客様に出会う事を指します。それには、「どこから新しい見込みのお客様を見つけるのか？」という問題が生じますよね。当然、道ですれ違った人に突然声をかけるわけにはいきません。私たちは日々の保全活動の中で、既存のお客様から紹介していただいて新しいお客様と出会っています。結局、すべては保全活動から始まっていると思います。例えば新規の行く先がなくなった場合、結局は自分の知り合いに戻る事になります。最近結婚した人、子供が生まれた人、新たな事業を始めた人など、自分の知り合いの中から探していくのが出発点です。その際には、「自分がこの仕事を頑張って続けているよ」という事や、「自分とお客様との距離感を近く保つ」事がここでも重要になると思います。なぜなら、課題を抱えた人や状況の変化があった人を知っているとして、それを教えてもらえるのは自分と近しい人だけだからです。新規の為の活動は、「こんな人を紹介してほしい」と伝えて、紹介できるような人を探してもらう事です。この点を切り離して考える事はできません。全てはやはり保全の中から、という事でしょう。</p>
              
              <p><span class="text-blue">聞き手：</span>先程の話題でも出ましたが、やはり「新規」と「保全」は切り分けて考えるものではないのですね。保全活動というのが下地にあってこその新規の為の活動につながるという事なのですね。ありがとうございます。最後の質問となりましたが、カレンダーに関して私ども制作会社に対する今後の期待値やご要望があればお聞かせください。ちょうど今日は撮影で八雲さんもいらっしゃいますので、池田さんのご感想をぜひお聞かせ願えたらと思うのですが、如何でしょうか？</p>

              <p><span class="text-blue">池田：</span>カレンダーはデザイン、八雲さんの写真、挨拶状、発送代行など大変満足しています。要望としては紙厚をもう少し厚くしていただければと思いますね(笑)。</p>
              
              <p><span class="text-blue">聞き手：</span>ありがとうございます(笑)。ご要望事項もありがとうございます。因みにカレンダーのお話を申し上げますと、今年で10年目です。もともと神戸支社様と神戸第二支社様とで30名の方にお使いいただきスタートしたものが、今では全社で1,100名の方にお使いいただいています。</p>

              <p><span class="text-blue">池田：</span>それはすごいですね。</p>
              
              <p><span class="text-blue">聞き手：</span>10年前を振り返りますと、もし神戸支社さんと神戸第二支社さんからカレンダーのお話をいただかなければ、このカレンダーも今の私もありません。その中でも池田さんにはずっとご愛用をいただき本当に感謝しています。ありがとうございます。</p>

              <p><span class="text-blue">池田：</span>いえ、とんでもない。こちらこそいつもありがとうございます。</p>
              
              <p><span class="text-blue">聞き手：</span>本日は大変貴重なお話をお聞かせいただき、誠にありがとうございました。今後とも引き続きよろしくお願いいたします。</p>
              
