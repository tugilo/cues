          <p><span class="text-blue">聞き手：</span>次に、保全活動全般に関してなのですが、LP様のなかには、「新規のお客様との時間が忙しくて保全になかなか時間が割けない」というお話を伺う事があります。もし後輩の方にこのような相談をされた場合、どのようなアドバイスをされますか？</p>

          <p><span class="text-blue">池田：</span>確かに私たちの仕事において、新規のご契約をお預かりしなければ長く続けていけないという面がありますから、どうしても保全よりも新規契約の為の活動に注力する傾向はあると思います。しかし、ある程度のお客様のボリュームがあれば、新しいお客様からのご契約よりも、保全活動から新たなニーズを見つけ出してそれを解決して差し上げる方が容易だと考えています。私の場合、売り上げの60％～70％は現在のお客様からの追加契約によるものです。保全活動をしっかりと行う事で自然と数字が挙がるので、私にとってはそこをきっちりやっていったほうがやはり楽です。とはいえ、新規の活動を完全に止めてしまうと成長しなくなりますから、新規の活動そのものは継続する必要はあると思います。もし私が保全の時間が取れないと悩んでいるLPに対してアドバイスをするとすれば、「保全は既定のルーティンとして行えば、売り上げの60～70％は自動的に伴ってくる。残りの30～40％は新規でどう組み立てるか、という話だよ」と話をするのではないでしょうか。</p>

          <p><span class="text-blue">聞き手：</span>「保全活動」と「新規の活動」という2つの言葉は、しばしば相反する意味合いで捉えられる事が多い印象がありますが、実は一緒でしょうか？</p>

          <p><span class="text-blue">池田：</span>そうですね。一緒ですし、まず保全の中から新規は生まれると考えています。お客様のニーズは常に変化していますし、ご加入当初のお考えと現在のお考えが変化しているケースもあります。お客様のニーズやご興味を的確に把握し、それにお応えする事で新たなビジネスチャンスが生まれる可能性があります。それが例えば5年先のご加入につながるのか、すぐのご加入なのか違いはあれども、保全活動を適切に行っていれば数字は確実に付いてくると考えています。</p>

          <p><span class="text-blue">聞き手：</span>そうしますと、定期的に通信物をお届けするというのは、やはり重要な取り組みになりますね。</p>

          <p><span class="text-blue">池田：</span>はい。とても大切だと思います。例えば、北海道のお客様とは年に1回必ず会うのは難しいですよね。そう考えると、通信物を5回送る事で1回実際に会ったのと同じ効果があって、お電話で1回お話しする事でいわば1.5回会ったのと同じようなイメージになると思います。ですから、例え5年ぶりに会ったお客様でも、あたかもつい最近お会いしたかのように、「実は今こんな状況なのですよ」と近況を教えてくださいます。もし何もしないままにものすごく期間が空いてしまうと、「いや、池田さん、今の私の状況何も分かってないでしょう？」みたいに、きっとお客様の心は離れてしまうでしょう。そのような状況が起こる可能性をできる限り排除するのが保全活動だと思っています。</p>
          
          <p><span class="text-blue">聞き手：</span>お会いしていない期間に、見えないところでお客様との距離を縮めているイメージですね。</p>
          
          <p><span class="text-blue">池田：</span>そうですね。例え、「ご無沙汰しています」というケースでも、前回お会いしてお話をさせてもらった時の状況に近いところに一瞬でパッと戻れるという効果はあると思っています。だからこそ、お会いしていない期間にお客様との距離感を縮める事はとても大事であると思います。</p>

