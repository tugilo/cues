          <h3>―― 最後に、私共に今後期待される事や、保全活動のサポートに関するご希望があれば、ぜひ期待値も含めてお聞かせいただきたいです。</h3>

          <p><span class="text-blue">神山：</span>八雲さんの写真が本当に素晴らしくて、毎年感動しています。とても高価なカレンダーみたい。でも、実はとても買いやすい価格ですからね。お客様からも喜ばれる本当に良いカレンダーだと思いますね。<br>美しい写真だけではなく、やはり便利なアンケートを利用出来るのが本当に良いです。僕たちLPの痒いところに本当に手が届くサービスになっています。自分でやると何かと面倒ですからね。それに一番労力が必要な発送作業をそのままお任せ出来るのも本当に助かります。僕はカレンダーに、年末の「英ちゃんTimes」を御社で同封していただいていますが、美しくて使いやすいカレンダーに、「僕の今年の重大ニュース！」みたいな楽しい記事が載った新聞も入るので、毎年お客様の認知度はとても高まっています。それは、毎回のアンケートの返信率にも反映されているように思います。<br>カレンダーのサービスについては、僕たちLPのニーズをとても良く理解し、考えて下さっているので大変満足しています。敢えて要望があるとすれば、カレンダーの写真を海外のものだけでなくて東京のプルデンシャルタワーなども入れてもらうとかどうでしょう。12月にはタワーの前に大きなクリスマスツリーが出るのですが、とても綺麗ですからね。あと、仙台のDCCとかも。「当社の間接部門がここにあるんですよ。」みたいに、LPの話題の幅が拡がると思うのですね。もし、一部だけ国内の写真になるのがコンセプト的に合わないのであれば、数年に一度とかでもいいかもしれないですね。</p>

