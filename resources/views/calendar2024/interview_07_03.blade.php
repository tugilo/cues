          <h3>―― コロナ禍を機に、メッセージアプリとかオンライン会議ツールなど、お客様とデジタルで繋がる手段が多様化しています。そんな中、新聞や私共のカレンダーなど、アナログ媒体をお客様にお送りする価値について、神山さんはどのようにお考えでしょうか？</h3>

          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol07_img03.jpg" alt="Special interview">
          <p><span class="text-blue">神山：</span>コロナ禍の下で人となかなか会えないとか、これまで当たり前だった直接対面でお話しする事が難しくなりました。もちろん、そんな時代だという事は当然理解出来るのですが、やっぱり僕たちの保険ビジネスはヒューマンビジネスだと思うのです。対面で会う事によって声のトーンとか、顔の表情とかそういったところでお客様の状況を理解出来るし、こちらの考えも伝わる。僕たちもお客様もリアルな距離感を感じる事はとても大切な事だと思うのです。<br>確かに事務的な事は書面で送ったり、オンラインや電話のやり取りなどで済ませられるかと思います。しかし、実際にお会いしないと感じ取りにくいお客様のお気持ちや困り事をどうくみ取るか、僕の心の底にあるものとか感じている事をどうお伝えするかが課題になります。やはり、お会い出来ない間の取り組みが大事になるかなと思います。<br>その点、「英ちゃんTimes」と、御社の「カレンダー」は、日頃なかなかお会いする事が出来ないお客様との距離感を縮める効果的なコミュニケーションツールになっていると思います。新聞は私からのメッセージを伝えられますし、カレンダーでは付与されているアンケートがお客様からのご要望やメッセージを漏らさず把握するのに役立ちます。両方ともコロナ禍以前からの取り組みですが、コロナ禍になって余計に、「やっていて本当に良かった！」と痛感しています。</p>
