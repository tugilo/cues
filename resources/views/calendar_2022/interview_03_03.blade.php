          <h3>―― 保全活動を継続していくためのコツ、あるいは意識されている事がありましたら教えてください。</h3>

          <p><span class="text-blue">田内：</span>まず、続けられない事はやらない。もし無理してやっていても、それを途中でやめてしまったらお客様はサービスの低下と思ってしまいます。私の場合でしたらお誕生日のお電話・エアメール・年末のカレンダーとカレンダーに同封する通信物を続けていますが、もし、それらをやめたらきっとお客様はサービスが低下したと思われるでしょう。私はそう思われるのが嫌なので続けています。少なくとも会社で言われているスタンダードサービスではないですが、やはり年に1度はご連絡します。連絡方法は電話に限らずメール等でもいいと思いますが、そんな事かなと思います。続ける事が出来ない事はやらない。そもそもまず始めないという事ですね。</p>

          <p><span class="text-blue">聞き手：</span>新規契約の活動の方が忙しく、保全活動になかなか時間が割けないという悩みを持つ、とりわけ若いLPさんから田内さんはそのような相談を受けた場合、どのようなアドバイスをされますか？</p>

          <p><span class="text-blue">田内：</span>「保全をしたい」のか、「保全活動をしないといけない」のか、という２つの動機のケースがあると思います。「保全をしたい」という動機も、「本当に保全をしたい」と、「新規に行きたいけど、行くところがないから保全から発展させたい」と、2種類あると思うのです。多くの人は、ついつい「見込客の発見が厳しくなっているから、保全活動の中から次の行き先を見つけてくる」という事をしがちです。もし、新規につなげる事が目的の保全だったら、「お好きなようにどうぞ」と言います。トークやテクニックを駆使するなり、お客様からお叱りを受けない程度に上手くやったらいいじゃないのかなと思います。でも、本当に「保全をしないといけない」という事であれば、いろいろ工夫出来る事はあると思います。例えば日々の細かい会話の中から、或いは年に1度の電話の中でも「今度結婚する」とか、「近く子どもが産まれる」とか、お客様の身の上の変化をお聞きする事は出来るので、それらをちゃんとメモして祝電をお送りしたり、PCSのアラート機能を活用しながら、<img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 my-4 my-lg-2" src="/images/interview/vol03_img03.jpg" alt="Special interview">次の電話の機会に「どうでした？」と聞いてみる。そうするとお客様も「あ、覚えてくれていたんだ！」と感じてくれます。そういう保全が大切じゃないかと思います。<br>実は私も以前は勘違いしていました。保全活動といったら、とにかく既契約のお客様のところに訪問して紹介をもらうみたいな。何か新規契約のため、見込み客発見のために既契約者を訪問する、それが保全だ、みたいに思っていました。しかし、在るべき保全活動とはLPとして保険契約のメンテナンスというのはもちろんですが、ご家族のイベント事とかステージアップとかチャレンジとかを一緒に経験するというか、楽しむというか、隣に寄り添うみたいな。そういう事がLPの保全活動の在り方じゃないかなと思います。例えば、会社の周年記念や、お店をしている人だったら何周年というのも、そういうところこそ気にしておきたいなと。お店を経営している人ならお花を送ってもらったら、きっと嬉しいと思います。営業色を出さない方がいいと思ったら社名はつけないとか。そういう心配りが大切になるでしょう。</p>
        </section>
