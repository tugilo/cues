        <section class="interview-box">
          <h3>―― 現在、新型コロナウイルスの影響で、LPの方々も大きな影響、活動制限が有られるようです。<br>まだ収束は見えませんが、こんな時こそ、LPとして保全活動の在り方や考え方などがあれば伺いたいです。（取材日付4月1日）</h3>

          <p><span class="text-blue">田内：</span>私たちはフルコミッションですから、不測の事態、それこそ自分が病気になり働けなくなって、その出来高がゼロになったら収入も限りなくゼロに近づきます。今回のコロナ災禍という不測の事態の中で、その危機感を感じているLPは多いと思います。<br>ただ、「大変だろうな、なんとかサポートの手を差し伸べてあげたいな」と個人的には思いつつも、「リスクを取って自分で選んだフルコミッションの世界、そんな事も起こるかも知れない中でやってきたのだから、すごく厳しいと思うけど甘えていてもダメだろう」と背中合わせで思う事もあります。この仕事はいつでもリスタート出来る仕事です。例え1か月仕事を止めても2か月止めても、もう一度ふりだしに戻って1からやっていったらいいじゃないか、と思います。新しい年度のスタートが6月になるのか7月になるのか判りませんが、そこでもう一度リセットボタンを押して、そして自分でスイッチを入れて進んでいきたいですね。<br>POJはみんな条件は一緒です。例えば、エグゼだからとか、キャリアが長いから特別に手数料が高いとか、そんな事は一切ありません。みな同じ条件で一緒にスタートするから、俺だけはとかそんな事は思わずに、一人一人ちゃんとリセットして、もう一度ふんどしを締め直してみんなで頑張っていけたらと思います。<br>お客様からすれば、私たちがどれだけたくさん売ったのかなんてどうでもいい事で、興味もない事です。例えば、私がPTになったという事は、ごく一部の営業会社の人は興味があるかもしれませんが、普通の方は「ふーん」で終わる話です。それよりも、こんな時だからこそ、真の意味で保全活動を通じてお客様の不安や心配な気持ちにきちんと誠意をもって対応しながら、もう一度はじめの第一歩を踏み出せたらいいんじゃないだろうか、と思っています。</p>
