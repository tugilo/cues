        <section class="interview-box">
          <h3>―― 井芹さんが後輩の方から「保全活動に時間が取れない」とご相談を受けた場合、どのように助言されますか？</h3>

          <p><span class="text-blue">井芹：</span>それは「保全」と思うから時間が取れないのではないかと思います。「保全しなくちゃ」とか思うからですよ。「新契約と同じ感覚で保全もしたらいいんじゃないの？」とアドバイスすると思います。「営業・新契約」も「保全」も一緒という事ですね。コミッションが発生しないと考えたり、言われる事もあるかと思いますが、一方では、ご契約いただいたお客様からは一番初めにコミッションをいただいている訳ですから、それをお返しすると言うか、いただいた以上のサービスは差し上げたいと思うし、それをする事によってまた次のご縁をいただける事もあると思います。それを「保全しなきゃ」とかと思うと、保全と新契約が別物と思うから時間が取れないという気持ちになるのではないかなと思います。<br>私の座右の銘という訳ではありませんが、LPの仕事をしていてとても大事にしている言葉がありまして、「一隅を照らす」と言う言葉です。通常、隅っこでもスポットライトが当たって、みたいな解釈で語られる場合が多いのですが、またちょっと違う意味があって1個の光はすごく小さいかも知れないけど、集まる事によってどんどん光が広がっていくよという意味も込められているらしく、意味を聞いた時、「あ、そうだな」と思いました。私たちの場合、最初のスタートは１人のお客様ですが、そこからご紹介が広がって私たちの保障にご加入いただくと言う、日々の活動の灯りがどんどん広がっていって大きな灯りになっていくという事をやり続けているから、もし消えそうな光があったら点けに行かなきゃいけないと思うんです。灯を灯しに行く事によって、その人から次の灯りがまた点くかも知れない。そういうイメージが保全です。手をかけなければ植物が枯れるのと同じで、お客様も手をかけなければ灯りは消えてしまうし、その灯りが大きくなる事も無い。全ては自分から始まった灯りをどんどん大きくしていきたい、というところです。「あら、あそこ消えそうだ」と思ったら今行かなきゃいけない。先程お話をしたように、保全から全ての新契約が始まるというのと同じで、自分の周りに今広がっている灯りが消えないようにしながら新しい光を見つけていく、そんな話ですね。<br>「偶然は準備の出来ていない人を助けない」とも言われます。これはルイ・パスツールという方の言葉らしいのですが、いつでも準備をしているからこそ、たまたまのチャンスを掴む事が出来ると思うのです。保全も同じで常にお客様に何があっても対応出来る、お応え出来ると言った準備をしておけば、次のチャンスも掴める。この灯りを消さないと意識をする、と言う事を考えると保全は是非やった方が良いと思えるはずですから、時間が取れないという考えは起きないと思います。</p>

          <p><span class="text-blue">聞き手：</span>「保全活動」と言う表現はひとつの意味だけで捉えられているような感じがされますか？</p>

          <p><span class="text-blue">井芹：</span>はい。保全の延長線上に新契約もあると思います。LPとしてスタートして、最初の１人のご契約を預かったら、この方を保全する事で紹介が出て、次の新契約が生まれます。この繰り返しだと思います。そんな風に思ったら、「保全の時間が無い」ではなく、自分を通じて保険に入って下さった方の事を思えばそんな気持ちにはならないのでは、と思います。</p>

          <p><span class="text-blue">聞き手：</span>だから、一隅を照らし続けるわけですね。</p>

          <p><span class="text-blue">井芹：</span>はい。だからと言っていつも会わないといけないとか、そんな事は無いと思います。自分の温度で、自分の感覚でその灯りが消えていない事がちゃんと確認出来ていれば大丈夫だと思うし、消えそうだなと思えば何かのSOSの信号かも知れない。それが察知出来るようであれば良いのではと思います。</p>
