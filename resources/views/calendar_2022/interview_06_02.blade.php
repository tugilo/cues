        <section class="interview-box">
          <h3>―― 保全活動についてこだわりの点をお聞かせ下さい。</h3>

          <p><span class="text-blue">井芹：</span>こだわりと言っていいのか、お客様にとって私という存在は、家族のようであり、親戚のようでもあり、或いは親友のように、必要な時にお話が出来るような、「いつも一緒に近くにいるよ」という存在でありたいなと思っています。お客様にはそういう感覚を持ってもらえるような努力をしています。なぜなら、私、家族が大好きで、両親も妹も夫も、とても大事にしているからです。きっとお客様にとっても、家族はすごく大事なものだろうし、とても身近な存在で何でも話せるのが安心だから、と思うからです。親戚のおばちゃんとか、実の姉妹みたいにちょっとしゃべってみよう、ちょっと話してみようとか、と言う存在でありたい、肌で感じて貰えるような人でありたいなと思います。<br>保全に関する取り組みは時間と共に大きく変わってきました。保険業界の一般論として、「新規契約・営業活動」と「保全活動」と、切り分けて捉えているような印象があります。LPとして17年目を迎える私の場合、どちらかと言うと新契約で新しい人を追い掛けるという事はほとんど少なくなっています。もちろん新契約は新契約で追いかけるのですが、それはいわゆる「保全活動」からすべて発生しているというイメージです。既契約のお客様の環境が変化し、ニードが変わって新契約になる場合もありますし、そのお客様の家族が増えて新契約になる場合もありますし、既契約のお客様から新しい世帯や新しい人のご紹介が発生する場合もあるので、全ては保全からスタートしている、というイメージです。何かあったら井芹に、と言って貰えるようなお付き合いをする事で新契約が生まれてくる、そういうスタイルに徐々に変わってきています。基本的には親子３代預かりたいというのが私のモットーで、若い方であればお孫さんまでいくと４代というところもありますけど、そちらをお任せいただけるようになりたいです。</p>

          <p><span class="text-blue">聞き手：</span>お客様は井芹さんにどのような関わり方を望んでらっしゃると思われますか？</p>

          <p><span class="text-blue">井芹：</span>期待されている事は良く判らないのですが、よくお客様から言われているのは、「井芹さんには全部任せているから」と言われる事が多いです。強いて言えば、必要に応じて私から連絡が有るだろうと。でも必要以上に思ってくれてらっしゃる方も多くてですね(苦笑)。最近は危険を感じているところも有るんですが (笑)。私から何も言ってこないという事は大丈夫だろうという感じで思っていらっしゃるので、全面的に信頼を置いてくださっていると言うか。もちろん、何かあれば全力で相談に乗ります。所謂、保険というものは人生におけるお金の問題の解決策な訳ですよね。その起こり得る問題の解決策である以上は問題が起きた場合、直ぐ解決出来るように、人生の要所でその確認だけはして下さいね、とはいつもお伝えするようにしています。</p>
        </section>
