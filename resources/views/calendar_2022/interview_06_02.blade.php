          <h3>―― オンライン面談が出来るようになって如何ですか？</h3>

          <p><span class="text-blue">井芹：</span>オンライン面談はとても有効だと思います。保全も新契約も全て直接面談だったものがオンラインで完結するので、お客様とお目に掛かれる頻度が上がるというのは間違いないと思います。慣れないオンラインを使っていると、本題に集中してお話するようにもなり、面談は短く済む事が多いですよ。「時間」と「場所」がワープ出来るのは本当に素敵だと思います。私のところは地方なので、直接面談ではお客様方まで車を使って片道1時間と言うのも珍しくありません。片道1時間掛けて訪問して、お話は20分で終わるという事もあります。それなら居ながらにして用件を20分で済ませられるオンライン面談は魅力的ですね。<br>よくオンラインだとテンション上げていかないといけないよみたいなお話もあるじゃないですか。でもテンションが上がるだけだとお客様も疲れてしまいますよね。私、使う前に色々なパターンをすごく研究したんですよ(笑)。LPさんと繋いでこんな喋り方はどうだろうとか、どういう風に顔が見えるだろうとか、どこに行ったら視線が下がらないとか、会っている感覚を出す為にどうした方が良いかとか、すごく研究したんです。判った事はテンションが高いのも良いんだけど、案外ゆっくりいつも通りでも安心感は与えられるし、それがオンラインの弊害にならないなという事でしたね。<br>オンライン面談時に工夫している点としては自分の「背景」に気をつけています。例えば綺麗な絵を飾ったりとか、「オンライン面談用の部屋ですか？」とお客様から聞いていただけるような環境整備ですね。<img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 my-4 my-lg-2" src="/images/interview/vol06_img02.jpg" alt="Special interview">面白いものを買ってきたりとかしたら後方にわざと置いてみて、聞いて貰えない時は自分で振ったりもします。<br>「この間、これ買ったんですよ。どうですか？」と言ってみたり、演出ですね。「こんにちは」と言ってもすごく仲良しの人だと話が直ぐに盛り上がるんですけど、久しぶりだと「早速始めましょうか」となってしまうので、「オンラインどうですか？」と言いながら「後ろも結構見えるんですよ」とか言ったり、「背景こうやって変えたりも出来るんですけど透明人間になったりします(笑)」とか言いながらやってます。今はこのような時期なので、意外とオンラインを選ばれる方も増えてきたなという感覚があります。環境(端末)はスマホが多い為、事前に資料を送ったり、注意を払っています。共有しても「広げられますか？」となって難しい時があるので。「何ページ開けて下さい」と言って「そこを見て下さい」とか、そんな感じでちょっとアナログですが、そういう風にお話しする方が多いです。</p>
        </section>
