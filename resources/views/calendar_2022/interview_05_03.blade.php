          <h3>―― 弊社の制作物に関して今後期待される事や、私どものような制作会社に対して関わりの在り方があればお伺いしたいです。</h3>

          <p><span class="text-blue">長曽我部：</span>先ほどもお話ししましたが、こちら(カレンダー)を使わせていただいて、あれだけのアンケートやお言葉が返ってくるとは思わなかったです。ここまでくると、次回はもっと返って来て欲しいなと思いますし、もっとお客様の方から色々なアクションをいただければそれだけで双方向のやり取りになりますから。そういう意味でこの自作挨拶状もそうですし、いわば自分の「自助努力」として、自分の出来るものをしっかり作り込んでいきたい。その上でカレンダーの発送代行サービスについては、その時その時で旬となるようなものを気軽に同封出来るような付帯サービスもあればより良いと思います。</p>

          <p><span class="text-blue">聞き手：</span>ぜひ、カレンダーそのものにも一言。八雲様がいらっしゃいますし(笑)</p>

