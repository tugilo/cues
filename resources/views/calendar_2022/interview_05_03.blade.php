          <h3>―― ズバリ一言、長曽我部さんにとって「保全活動とは？」</h3>

          <p><span class="text-blue">長曽我部：</span>「保全活動とは？」ですか…(苦笑)。「保全とは」ならば、「一番の保全は長くLPを続ける事」ですが…。そうですね、顧客の創造でもありますね。僕が捉えている保全の目的は2つ。お客様への保証「ごんべんさん」の「保証」と、新契約に繋がる面です。この2つの目的があるのですが、「保全活動とは？」をズバリ一言で言い表す言葉とは何でしょうね・・・やはり「価値観の共有」でしょうか。価値観の共有はとても大切です。Facebook「日出ずる国の営業」にも僕は「価値観の共有」と上げていましたね。</p>
        </section>
        <!-- n:interview-box -->

        <!-- s:interview-box -->
        <section class="interview-box">
          <h3>―― キャリアがまだ浅く、若い世代のLPの方々へのメッセージを是非、お願いします。</h3>

          <p><span class="text-blue">長曽我部：</span>続けられない事はしない事。もちろんキャリアによって違うと思いますが、キャリアのごくごく浅いTAP中の若いLPに対しては「脇目も振らずに一生懸命働け」と。これは本当にそう思います。前職を一大決心して辞めてきているので、その位の気概を持ち、その勢いで最低2年間は仕事をして欲しいと思います。キャリアと共に段々やっていただきたい事は「自分の土俵を見つける事、もしくは作り上げる事」ではないかと思います。それさえ出来れば、この仕事でやり方に悩む事はあっても、仕事に対する悩みは少し薄らいでくるのかなという気はします。<br>自分の土俵を作る。これは2つあると思うのですが、1つは誰しもが同じように出来る訳では無いのですが、当然の事ながら自分のスキルと知識を活かして得意な事をやっていく、勝負していく事ですね。もう1つは丁度先ほど「お客様との価値観の共有」という言葉が出ましたが、お客様の価値観を飲み込めるかどうか。自分の価値観をお客様が飲んでいただけるかどうか。一言で言うと「合う人」ですよね。合う人のコミュニティをどれだけ作っていくかという事だと思います。それが趣味なのか性格なのか地域なのかは人それぞれ有ると思いますが、<img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6  my-4 my-lg-2" src="/images/interview/vol05_img03.jpg" alt="Special interview">自分がそこにいて、無理をせず心地良い状態でお客様とリレーションが築けるかどうかと言う、そんな土俵です。それさえあれば大きく崩れる事はなく、ずっとこの仕事が続けられるだろうと思います。</p>

          <p><span class="text-blue">聞き手：</span>スランプや壁はいつでも必ず出てくると思いますが、その際はどのような考え方をして乗り越えていきますか？</p>

          <p><span class="text-blue">長曽我部：</span>壁という表現で言うと、乗り越える方法もあれば、避けて通る方法もありますよね。裏から回る方法もあります。多くの場合は「乗り越えて、乗り越えて」と思ってしまいがちで、でも「乗り越えて」と思うと大変辛い事ってあるじゃないですか。だったら避けて通ってしまえ。要は逃げてしまえという事ですね。それも1つの手ですよ。見極めを持って、出来ない事はやらない。自分が最大限の力を持って出来るところで土俵を作る事が大事です。</p>
        </section>
