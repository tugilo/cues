      <section class="interview-box">
        <h3>―― 中村さんが普段取り組んでいらっしゃる保全活動について教えて頂けますか？</h3>
        <p><span class="text-blue">中村：</span>公務が忙しいため、年に一度のバースデーコールと、プルマサニュース＋キューズプラスさんのカレンダー・アンケートをお届けしています。それから、毎年5月・6月・8月に会社行事が海外であるので、その前の段階で「〇月〇日から〇月〇日まで海外にて留守にします」という案内ハガキを出しています。「24時間、電話は対応できますが、海外に行っておりますので対応ができない可能性もあります。」という文言を入れてお客様にハガキを送っています。</p>

        <p><span class="text-blue">聞き手：</span>そのハガキは文言だけではなく、何か写真を添えたりされていますか？</p>

        <p><span class="text-blue">中村：</span>私の顔写真を一緒に入れます。お客様が私の顔を忘れないように。「顔写真は要らない、ハガキだけで十分事は足りてる。」と言われるお客様もいますが、あえて顔写真は入れます（笑）。</p>

        <p><span class="text-blue">聞き手：</span>海外に行かれる都度、ご案内されているわけですね。</p>

        <p><span class="text-blue">中村：</span>毎回ではなく1回だけです。5月のPTC前の段階で、5月・6月・8月の予定をすべて記載します。毎回送ると費用もかかりますし、お客様も「これ見よがしに海外に行くのか！」となるので1回だけです。ですから、「この期間中は留守にしているな」とそのハガキを置いてもらうことによって、もし保険の話が出た時などに私のことを思い出してもらえることになります。</p>
      </section>
      <!-- n:interview-box -->

      <!-- s:interview-box -->
      <section class="interview-box">
        <h3>―― お客様向けに毎年末、「ニュースプルマサ」を制作されていらっしゃいますが、そのクオリティの高さは多くのライフプランナー様の間でも有名になっています。<br>これをお作りになった経緯、時期と内容、こだわりについてお聞かせください。</h3>
        <p><span class="text-blue">中村：</span>まず、いつ始めたかですが、1999年2月頃です。私は1998年の5月入社で、6月から営業をスタートして以来、毎日保険を販売し続けていましたので、「これはおそらくお客様のところに訪問できないな」という懸念があったからです。因みに、プルマサニュースの1号は、かわら版みたいな形でスタートし、当時はB4サイズでした。</p>

        <p><span class="text-blue">聞き手：</span>20年間続けていらっしゃるのですか？</p>

        <p><span class="text-blue">中村：</span>20年間続けています。創刊当初は年2回ぐらいだったのですけど、今は年1回になっています。</p>

        <p><span class="text-blue">聞き手：</span>もともとＢ4サイズのかわら版で始めて、今はどのような形になっていますか？</p>

        <p><span class="text-blue">中村：</span>A4で横型にさせていただいたり、映画のパンフレットみたいな冊子になったり、毎回工夫しています。「ご契約をお預かりしても自分がなかなか行けない、申し訳ない。」という担当者としての意識が動機付けとなって続けています。自分のことを忘れられても困りますし。「プルデンシャルの中村将幸から加入していただいている」という担当者意識、当事者意識を強く持ったことがきっかけです。お客様にとっても、何かあれば、すぐに私に電話をかけやすいようにという意図があります。当時はカスタマーサービスセンターがなかったですから。</p>

        <p><span class="text-blue">聞き手：</span>もともとの「かわら版」から現在まで、お作りになる目的などは変わっていないですか？</p>

        <p><span class="text-blue">中村：</span>目的は変わりません。お客様から「自慢新聞」と言われることもありますが（笑）、自分の担当者は頑張っていますよ、というひとつの投げかけです。それと、オリジナリティです。ライフプランナーは以前2000人弱でしたが、今は4000人を超えています。埋没しないための区別化、差別化です。「私から入っていただいている」という自分のブランディングでもあります。</p>

        <p><span class="text-blue">聞き手：</span>中村さんのお客様は本当に幸せだな、と見ていて感じます。いろいろなものが毎年年末に送られてくるのを楽しみにされているお客様はたくさんいらっしゃると思います。</p>

        <p><span class="text-blue">中村：</span>ありがとうございます。しかし、お客様の立場で考えると「プルマサニュースを送ってもらうよりは来てくれたらいいのに。」と思う方もいらっしゃると思います。そこでアンケートが重要なわけですが、カレンダーがあれば、開封されずアンケート回答もされずにごみ箱にいくことはないと思うので、キューズプラスさんのカレンダーのおかげでアンケートの回答率が高まっているのは間違いないと思います。それに、カレンダーは時期的に必要なものですから、コラボするメリットも大きい。新聞だけなら「いいよ、どうせ自慢新聞だろ。」となってしまうかもしれません。<br>また、新聞の狙いは「またこんなパロディチックなことを！」というインパクト・印象を残すことなので、非常に美しい写真のカレンダーと一緒に自分のニュースを送ることによって、その相乗効果は計り知れないと思いますし、非常に効果的と思います。</p>

        <p><span class="text-blue">聞き手：</span>最近はデジタルコンテンツが主流となり、あえて通信物として紙媒体を選んでいるような感じがしますが、紙媒体を用いることに明確な意図があるのでしょうか？</p>

        <p><span class="text-blue">中村：</span>お客様の層も0歳から70歳までいるので、アナログでしか見られないという人もいらっしゃるわけです。デジタルしか見ないという人は少ないと思います。だから、万人に不公平なく公平を保つのであればアナログの媒体。特にお年を召されている方は字が小さいと目を通してもらえないです。また、スマートフォンかガラパゴス携帯か、というお客様側の環境は様々ですから、ある意味公平になることを考えた場合は、紙媒体になるのかなと思います。また、私自身にとっても「こういうことをしている」という、ある意味自信にもつながります。<img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 my-4 my-lg-2" src="/images/interview/vol02_img01.jpg" alt="Special interview">いつもバースデーコールの時には「郵便物ばかりで申し訳ないです。」というフレーズから入るのですが、お客様からは「中村さんが忙しいのはわかっているからね。」「今は海外じゃなく日本にいるのだね！」ということを言われます。忙しくしているということをご理解してもらえるので、お客様にお電話を差し上げる際に、ご無沙汰ゆえ…という類の躊躇をせずに済みます。お客様へご案内したい商品があります、お話したいことがありますといった時に、ちゃんとお時間を確保してもらえることにつながります。しないよりはした方がいいと思います。<br>もし、これが何もしていなかったら、「保険を販売する時だけ連絡してくるのか？」ということになります。つまり、お客様に伝えたい情報があるのに、即座にお伝えすることが出来ないということになってしまいます。</p>
      </section>
