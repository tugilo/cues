        <section class="interview-box">
          <h3>―― 今後CX(顧客体験)を重要視していくというお話を伺っているのですが、神山さんにとって「理想的な顧客体験」というのは、ズバリどうお考えでしょうか。</h3>

          <p><span class="text-blue">神山：</span>いわゆる、「あるべき顧客体験」というものですね。実は本社から、「顧客体験」の考え方がアナウンスされた際に、「やっぱり、これからはこれだよな。」という風に僕は思いました。<br>我々は、やはり最初は保険の営業としてお客様とお会いするわけです。でも、お客様からすると、最初は保険の営業だと思っていたら、その担当が意外と自分たちの事を聞いてくれる。売り込みというよりも、いろいろと聞きに徹してくれる。そして、自分たちに合ったプランを提案してくれて、「ちょっと普通のセールスと違うな…」と感じてもらっていると思います。そこで納得をしていただいてご加入、お客様になっていただく。これが最初の段階ですね。次の段階は、そこから少しずつですが、「この人と話していると楽しくなってくる。」という段階に入っていきます。折に触れて、「もし、将来こういう人生を送るなら、今こんな風にしたらどうですか？」というアドバイスを差し上げたりすると、「この人と話していると、ワクワクして楽しいな。」となってくるわけです。それで、「何で楽しいのかな？」と思ったら、このLPはいろいろ趣味があって、LP自身が人生を前向きに楽しんでいて、そしていつも楽しそうに仕事をしていて…。そうするとお客様は、「自分もそういう風になりたいな。」みたいに担当者(担当LP)への見方が変わってくると思います。その中でプルデンシャル生命の保険に加入して、LPに担当してもらっているから、もし万一の事が起こっても安心という事だけに留まらないという事を感じていただけます。LPの生き方や役立つ情報に接して、「私たちも前向きに頑張らないと！」と思っていただける。それがあるべき顧客体験じゃないかと思いますね。<br>保全を重ねるうちに、だんだんとお客様はLPと話をするのが楽しくなって、人生にも前向きになる。それでこそお客様満足度も上がっていくのではないかと思います。後輩たちに伝えたいのは、「保全活動はたしかに次のビジネスの起商に繋がるけれども、起商を目的に保全をするのは違う。」という事です。もし、保全活動が追加契約のクロージングばかりとか、お客様に会うたび、「誰か紹介してくださいよ。」ばかりではどうでしょうか。顧客体験(CX)という観点で考えると、自ずと在るべきお客様との関わり方が見えてくると思います。</p>
        </section>
