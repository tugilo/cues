          <h3>―― お客様が保険にご加入後、LPの皆様にはどのような関わり方を期待されていらっしゃると思われますか？</h3>

          <p><span class="text-blue">神山：</span>やはり、クイックなレスポンスが一番大事かなと思います。給付金請求とか、内容を再度説明してほしいといった場合ですね。<br>お電話いただいたら直ぐに、もし取れなくても直ぐに折り返し電話出来るようにして、その後の対応が迅速に出来るという事が一番大事かなと思います。<br>
          それに、ちょっとした困り事の相談なども気軽に連絡が取れる存在と言いますかね。先程もお話したようにいつも身近に感じられて、相談したらちゃんと適切な答えが返ってくる。お客様はそんな距離感を担当に求めているのではないかと思います。ご加入後、常に「紹介したい人がいる。」とか、「追加で保険に入りたい。」という方は、そんなにいらっしゃらないと思います。だから、こちらの都合だけで距離感を詰め過ぎるのも良くないと思います。</p>

          <p><span class="text-blue">聞き手：</span>何かお困り事があったら直ぐにご連絡いただけるような距離感を常日頃、保っておくという事ですね。お客様はLPの方々に毎日お会いになるわけではないので、必要な時、直ぐに連絡出来るような、「精神的な近さ」を期待されていらっしゃる、と言う事でしょうか？</p>

          <p><span class="text-blue">神山：</span>そうですね。例えば経営者の方なら事業承継の相談、悩みとか愚痴を聞いたりする事も多いです。こういうのも僕が気軽に相談出来る存在だから、というのもあると思います。<br>資産の話題については、今の世情などと照らし合わせて、「僕は今こうしていますよ。」という話を差し上げるケースは結構あります。ドクターの方や経営者の方は日ごろ本当に忙しいので、「将来こういう風に人生をエンジョイすると楽しいと思いますよ。」といった話をさせてもらうと、「じゃあ、どうしたらいいの？」という風にご興味を持っていただいたりします。将来楽しく生きていきたいと考える時に、「じゃあ、今何をしたら良いと神山さんは思う？」という話ですよね。</p>

          <p><span class="text-blue">聞き手：</span>「神山さんのようになれたらいいなあ」、みたいな感じですか？</p>

          <p><span class="text-blue">神山：</span>そんな偉そうなものではないですけどね。でも、お客様と一緒に明るい将来の在り方を考えるのは楽しいですよ。僕自身が人生を楽しみたいですから。</p>

          <p><span class="text-blue">聞き手：</span>神山さんが活き活きと楽しそうにしていらっしゃるから、お客様が神山さんを頼りにされる。</p>

          <p><span class="text-blue">神山：</span>いやいや、ありがたい話です。</p>
        </section>
