        <section class="interview-box">
          <h3>―― 「保全活動を継続していくコツ」のようなものがあれば、教えていただきたいです。</h3>

          <p><span class="text-blue">神山：</span>コツといえるかどうか分かりませんが、お客様それぞれの人生に寄り添いながら、必要と考えられる情報を提供し続ける、そういう姿勢を示し続けること、ではないかと思います。これには少し前提をお話ししなければいけないですが、数年前から、「人生100年時代」の到来が話題になる事が増えていますよね。僕たちの世代では、いわゆる定年からが第二の人生になるわけです。その第二の人生を楽しく生きるためには、有形資産である金融資産や不動産といった形ある資産だけではなくて、無形資産、つまり人脈とか知識、趣味などといった目に見えない資産の存在が大きく影響すると僕は考えています。<br>理想的な保全活動とは、「お客様のライフプランを、お金の面と、お金では測れない面の両面で、将来も安心して楽しく人生を送る事が出来るように関わり続ける事」ではないかと思っています。例え大きな有形資産があっても、無形資産が無いために結局は寂しい老後を迎える方が世の中には結構いらっしゃるのではないでしょうか。問題はバランスです。60歳で定年を迎えた後、仮に100歳まで生きないとしても80歳くらいまでは元気でいらっしゃる方は多いと思います。その時に、どう生きているか、どう生活しているか、という事が重要と考えています。<br>人生100年時代を楽しく生きていくために、僕が道しるべではないけど、<img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 my-4 my-lg-2" src="/images/interview/vol07_img02.jpg" alt="Special interview">「人生こんな風にしたらいいのでは？」という話を差し上げたり、お客様と一緒に実践が出来たらいいなと思っています。</p>

          <p><span class="text-blue">聞き手：</span>とても深いお話ですね。どちらかというと保全活動というのは営業的な意味合いで捉えられる事が多いと思います。「お客様と接触する機会を増やす」みたいな。しかし、神山さんの今のお話では、お客様に人生の道標となるような判断材料をお伝えしていくという事が保全活動の指針、実践という事なのでしょうか？</p>

          <p><span class="text-blue">神山：</span>僕も60歳を超えて、「もっと営業をしよう。」というのが余りなくて。それよりも、お客様が将来どういう風に生きていきたいのかに関心があります。私の情報は、あくまで一つの判断材料として活用していただければ良いと思います。将来のために考えておいた方が良い事は、何も保険の事ばかりではありません。そういったお手伝いも僕の出来る範囲で出来れば良いなと思います。保険だけちゃんとすれば将来安心で楽しい、というのは不可能ですからね。当たり前の事ですが。</p>
        </section>
