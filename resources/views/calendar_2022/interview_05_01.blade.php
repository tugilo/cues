        <h3>―― 長曽我部さんが普段取り組んでおられる保全活動についてお願いします。</h3>

        <p><span class="text-blue">長曽我部：</span>いわゆる一般的な意味での「保全活動」で言われますと、現在は特別な事は行っていません。勿論、通常業務の中でお客様に対して電話を掛けたり、「どんな風にお過ごしか」というコンタクトは取りますが、バースデーカードをお送りしたり、というような事は今はやっていません。以前は行っていましたが、ある時全部やめて、今は完全に今年からですがカレンダーのみにしました。その理由は「続けられない事はやめよう」と思ったからです。<br>私は保全活動には２つの側面があると思っていて、ひとつめは、坂口さんがよく仰っていた「保障」と「保証」という、「こざとへんさん」と「ごんべんくん」の話で、「ごんべん」の「保証」は、「約款上は責務ではないけれども、お客様に対して我々が約束している事」です。つまり、長く「安心感」を持っていただく事です。長い間担当させていただく中でお客様のニードの変化等に対して適切に対応していく事、そして私たちの最後の約束である「保険金をお届けするその時」までの長きに亘って、お客様に安心感を持っていただく為の活動全般が「保全」ではないかと思います。一方でやはり「新たなお客様の創造」といいますか、既契約のお客様に新たなお客様との出会いを作ってくださるような投げかけをする。これも保全活動の目的ではないかと考えています。例えば私たちライフプランナー(以降LP)は個人単位で動いています。アシスタントを採用しても活動の時間にはやはり限界があります。極端な話ですが、保有が多くなると年に一度全てのお客様にお会いする事は時間的に不可能です。しかし、これを可能とする方法が一つだけあります。それはお客様を「グループ化」する事です。判り易い例は、職域のお客様です。例えば職域に20名のお客様がいて、その会社に一度お邪魔すればその20名の方とお会い出来ます。働ける時間が有限であり色々な制約があるからこそ、敢えて言えば「効率良く」ご契約をお預かりしながら、既存のお客様にも安心感を持っていただくような活動を考えるべきだと思うのです。もはや保全とか新契約とか活動を色分けするのではなく、どちらもが合体したようなイメージで。私の考える保全の目的は、既契約のお客様には安心感を持っていただく事、そして新たなお客様の創造を既存のお客様との関わりの中で続けていく活動、そんなイメージです。<br>これまで保全と言えば「顧客接点を何回持つか」と捉えていたところがありました。例えば、年賀状を差し上げると「これで年間の顧客接点は1」「春もしくは夏に季節の挨拶を出したら2」のように数えると、年間に大体3回ぐらいは顧客接点を持っていたようなイメージです。しかし、今年は思い切って年賀状をやめました。これまでずっとカレンダーと年賀状をお送りしてきましたが、12月に「今年もお世話になりました」とカレンダーが届き、年が明けて1日か3日には「あけましておめでとうございます」と年賀状が届く、という事で「はたして必要かな？」と思い、やめました。<br>今後はキューズプラスさんのカレンダーに付いているお客様アンケートをもっと上手に活用したいと思っています。この取り組みを始めたのは一昨年からですが、最初はアンケートの返信が余りありませんでしたが、今回は20通以上も返信がありました。今回が前回と違うのは、初めて「自作挨拶状」を同封した事です。簡単な一枚ものの挨拶状と加えてマスクケースを1枚ずつ同封したところ、お客様から多くの反応がありました。それらがアンケートの返信数にも影響したのではないかと思います。特にマスクケースはカレンダーとお送りした当時、タイムリーなものであった事も有り、お客様からは大変な喜びの声で、アンケートを返信いただいたお客様以外の多くの方からも好評のメッセージが寄せられました。私は「もうひと工夫」する事で、お客様からコンタクトしてもらいやすい環境を自身で整える事が出来るのではと感じていて、工夫する甲斐がありそうですね。個人的には、カレンダー(のお届け)は無限の可能性を秘めているツールかも？と思っています。<br>私以外でどれくらいのLPがこうした工夫をしているか判りませんが、「自作挨拶状」のようなものをそれぞれが作って、カレンダーに同封をお願いするのはとても有効ではないでしょうか。初めて故とても簡単な内容でしたが、今後は自作挨拶状の中に世の中のトピックスだけでなく、自身の私的なトピックスを入れたりして、更なる可能性に取り組もうと考えています。<br>SNS等では、コロナ禍を受けてのこの1年間、スポットでTeamsを使ってコンタクトも行いました。<br>それ以外には、これが私にとっての保全と言えば保全なのですが、先程お話しした保全の目的である「新しい顧客の創造」という事と「既存顧客の安心感」を両立する為、お客様とよくグループ交流をしています。いや、交流というより遊びかもしれませんね。今回調べてみると12グループありました。もちろん、これは特定のお客様だけを対象にしたものではありません。その12グループの中には150人ほどいらっしゃって、そのほとんどは経営者です。<img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 my-4 my-lg-2" src="/images/interview/vol05_img01.jpg" alt="Special interview"><br>このグループで、例えば旅行に行ったり、ゴルフに行ったり、ワインを飲んだりしています。旅行のグループ、ゴルフのグループ、ワインを飲むグループ。実はこの3つが僕の今の柱なのですが、自分はどんなお客様と一緒に仕事がしたいか、どんなお客様と一緒に成長していきたいかと考えた時には、やはり気の合う仲間と一緒にいたいのです。もし、ここに価値観の全く違う方のご契約をお預かりしてしまうと、もしかしたらそれに振り回されて本来やらなければいけない事が蔑ろになってしまう可能性があります。働ける時間が決まっていて、やれる事が決まっているなら、自分の価値観と合う人、或いはお客様の価値観に合わせられるような方とずっと一緒に生きていきたいなと思うのです。それこそがもしかすると一番の保全活動なんじゃないかと。想像するに自分の土俵ではないところで勝負をしている人もいるかもしれないですが、それってやはり続かないような気がします。一番の保全とは、長きに亘ってLPを続けて保険金をお支払いするまでお世話させていただくのが仕事ですから、長く続くお客様と一緒にいたい。そんなふうに考えると、そういうグルーピングを新契約の段階からやっていくことは結果として大きな保全活動に繋がるのではないかと思っています。</p>
