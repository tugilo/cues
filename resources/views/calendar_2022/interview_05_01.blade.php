        <h3>―― 保全に関する具体的な取り組み方は年月と共にどのように変わりましたか？逆に変わらない考え方もお伺いしたいです。</h3>

        <p><span class="text-blue">長曽我部：</span>まず変わらない事は私、この業界、この会社に入って最初は前職の先輩、後輩、友人それから高校大学の友人などベースマーケットの方々から保険のお話をさせていただきました。その方々にご契約の後に必ず言ってきた言葉があります。それは「私は普段ここには来ません」という言葉です。当時はまだ国内系保険会社の主に女性営業職員の方が会社を訪問して、飴やガムなどを配られてバイオリズムを書いてもらって、その後保険契約をお預かりするというスタイルが主流だった頃でした。そんな中、私たちはアポイントを取って色々なお話をお伺いして最終的にご契約をお預かりする。そして最後に証券をお渡しする時に言う言葉が決まって「私は毎日来る事はないです。もしかしたらもうお会いする事はないかもしれませんし、次は受取人さんとお会いする時まで会う事はないかもしれませんよ」と極端に言うとそれくらいのことを言ってきました。<br>ただ、間違って欲しくないのは「私は常にここにいます。だから何かあった時、若しくは本当に困った時には私は飛んできます。だから、それだけはどうぞ安心してください。普段は『長曽我部の顔見ないな』と思っても気にしないでください」というイメージでお話をさせていただいていました。その点は今も変わっていません。<br>余り最近は使われていないかもしれませんが、当時は「We’ll be there, when you need us most.」という言葉がありまして、これは色々なプルデンシャルのロゴの中にも入っていたような言葉です。これは何かというと「あなたが本当に必要とされる時、私はいつでもここにいますよ」という文言なのですが、その気持ちは一切変わってないという事ですね。ただそうは言いましても、「保全活動」という事について当時の先輩などから色々聞いていたので、最初はバースデーカードを送ったりもしました。他にも当時はお花を風船に一本入れて500円で送ってくれるサービスがあって、実はそれも始めてはみたのですが2～3年でやめました(苦笑)。それは何故かというと、毎年同じお花が届くからです。お客様も最初はびっくりされるのですが、段々マンネリ化してくる。ですから「これ何か意味あるのかな？」と思ってやめました。手書きのハガキも色々な先輩方がやっていたので、同じように始めてはみたものの、これも続ける事が出来なかった。つまり自分の性に合わなかったのだと思います。それならば「続けられない事を始めるのはやめよう」という感覚になりました。そこは大きく変わってきた点ですね。多種多様な取組、考えはありますが、やはり最高の保全というのは「いつ何時、何があっても、僕はあなたの傍にいますよ」という事を実感していただく事ではないかと思いますね。そして、長きに亘り私たちはそれを実践していく事。具体的にはお客様のニードの変化、或いはちょっとしたお困り事に対して、私たちが直ぐ手を差し伸べるような体制が取れている事。このような事が一番重要なのではないかと思います。後はお客様にこれらを感じていただければいい訳です。「あいつは普段顔を見ないけど、大丈夫。いつもここにいる」と思っていただけるような仕掛けさえ出来ればいいのかなと思います。それが先ほどお話ししたグルーピングでの交流や、カレンダーに同封する自作挨拶状や顧客アンケートなどのツールを活用したちょっとしたやり取りになるのです。</p>
      </section>
