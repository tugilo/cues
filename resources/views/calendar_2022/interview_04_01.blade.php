      <section class="interview-box">
        <h3>―― 1週間の活動時間で、新契約と保全活動の割合はいかがでしょうか？また、岡崎さんはLPとして10年以上のキャリアをお持ちですが、保全活動に関してお仕事のキャリアと共にどう変化されたか、一方で基本的に変わらないお客様への考え方があれば教えてください。</h3>

        <p><span class="text-blue">岡崎：</span>新規の活動が６割、保全が４割ぐらいです。新規の中には新規のお客様に商談のステージに上がっていただくための活動も含みます。<br>取り組みの変わらない部分からお話すると、私はずっと同じ言葉を使っているのですが「感謝と気付きのＦＦ」を提供することです。初回面談の時、お客様は保険のセールスパーソンに会って自分がどういう気持ちになるかなんて、想像もされていなくて、「なんとなく家計や保険のことが気になるから聞いてみよう」くらいの感覚です。でも商談が終わった後には、私と会話をすることで、まず家族のことがすごく大切に思えるようになる。「こうして周りの人が何々してくれるから、自分はこう在れるのだな」と気付かれるのです。例えば「自分が心置きなく残業出来るのは、奥さんが家を守ってくれているからなんだな」「母が実家で元気に暮らしてくれているから、今自分は仕事に専念出来るんだな」など。普段当たり前に思えている環境がいかに自分の生活を守ってくれているかに気付けると、お客様の表情が明るくなるんですよね。私たちLPは日々様々なご家庭の悩みや困難な状況に直面します。だからこそ客観的な立場で、お客様の「当たり前」の幸せを気付かせてあげられる存在なのだと思います。その上で大切な家族と自分の老後を守れる保険内容に加入出来ると、心の平和を得ていただけるのです。<br>保全で訪問した時も話題は違えど同じことをします。「もう3年生ですか！お子さんが元気に学校に行ってくれるって最高ですね！今は不登校のお子さんもたくさんおられるそうですから。もしそうなるとお母さんは気になってお仕事に出ることが出来ないかもしれませんよね」などと何気ない日常が実は一番ありがたいことに気付いていただく。それを雑談という形のＦＦの中で繰り返していく。そこが私の変わらない部分です。お客様は加入時にはもちろん保障内容にご納得はされています。ただその時はとても良いと思っていたとしても、2年も経てばお忘れになっていらっしゃいます。ですから、契約内容の確認はお客様にとって「過去の自分への承認」でもあります。自分のあの時の決断は正しかったのだと。私の場合は、得意な手書きニード確認書を活用して、「こういうことをお話しましたよね。結果こういう保険が持てたらいいなということだったので、このような保障内容になっています」とセリフ口調で当時を振り返りながらお話するようにしています。そして契約時お客様が保険加入の決断が出来たことを私が称賛すると、お客様自身も過去の自分を誇らしく感じていだけるようで、新しい特約のご案内にもスムーズに耳を傾けてくださいますし、「お前も岡崎さんの話を早く聞いておいた方がいいぞ」と自然とお子さんやご兄弟、同僚など身近な方へのご紹介にもつながります。<br>逆に変わってきたなと思うことは、保険以外のお役立ち精神です。POJに32歳で入社した当初は、お客様が年上なことも多く、本当に「あなたの人生を聞かせてください。それを勉強させてください」という感じでした。LPになって社内外の研修に参加するようになり、たくさんの学びを得ました。正直なところ、何百万もの費用を払ってでも学び続けられたのはお客様からお任せいただいたご契約から発生するありがたい報酬のおかげです。キャリアの途中からではありましたが、自分が学んだことは出来る限りお客様に還元しようと考えるようになりました。学びの多くはマインド、生き方に関するものでしたから、「元気の種をまきに行く」ことを保全のテーマとし、自分がとってきた情報は出来る限りお客様にシェアする、具体的には研修で受けた内容を私が一回かみ砕いて、取捨選択しお客様の背景や問題点に必要な部分だけを出してお話しします。そもそも生命保険とは収入が途絶えることに対するお金の対策ですよね。折角なら元気な時も入ってくる収入が上がった方が良いですよね。収入はマインドが上がると上がりやすい。目の前の問題に悩んでいる方に「こんな風に成功してる人もいますよ」「こんな考え方も出来ませんか？」と投げかけ、少しでも視野広く、元気に生きて欲しい、そういう関り方も大切にしたいと考えています。</p>
