      <section class="interview-box">
        <h3>―― 岡崎さんが普段取り組んでおられる保全活動について教えていただけますか？</h3>

        <p><span class="text-blue">岡崎：</span>私は保全活動を「攻めの保全」と「守りの保全」という二つの視点で分けています。「守りの保全」として全既契約の方に均一的なサービスを提供するという意味の活動は、契約して２年目の契約確認訪問です。これは証券をお渡しする時に、お客様に予め「2年後に契約内容のおさらいに伺いますね」とお約束します。２年くらい経つと保険内容をすっかりお忘れの方が多いので大変喜ばれますし、約束を守って訪問することも信頼につながると実感しています。その後は２年ごとにお電話し、お客様から「会いたい」と言ってくださったタイミングで訪問するようにしています。実は入社から数年は「２年ごとに必ず訪問します」とお伝えして、4年目、6年目と忠実に実行していました。しかし、保有件数が増えてきたことや、個人的な理由なのですが認知症である母の介護に、より多くの時間を使いたくなったこと。そしてお客様の生活の変化が必ずしも２年ごとに起きるわけではないという理由で今のスタイルに修正しました。その変わり証券手交の時に私が作った「お客様向けお金の相談会」のご案内をお渡しします。この内容は後ほど「攻めの保全」の時に詳しくご説明します。<br>通信物としては年４回のニュースレター（年末はカレンダーと共に送付）です。これは大判のハガキスタイルでお客様は封筒を開ける手間もなく、便利だと思って活用しています。制作元は業者さんですが自分のプロフィール写真や、ライフプランナー(以降LP)としての思いを伝えるオリジナルのコラム欄も載せられるのでお客様からもご好評です。キューズプラスさんとの提携があり、発送代行もしてくださるので手間が掛らず助かっています。年末にカレンダーをお送りする時には、特別に「年末のごあいさつ文」も作成します。写真付きで私の１年の活動をご紹介したり、お客様からいただいたコメントを載せたり、岡崎流夢を叶える３つの方法を披露したりと毎年喜んでいただけるよう内容を工夫しています。それと全ての方ではないのですが、年賀状も楽しみにしてくださっている世代の方々にはお送りしています。<br>また私のお客様はFacebook(以降FB)世代も多いので、お誕生日には「おめでとうございます。よいお誕生日を♪」みたいなコミュニケーションも取ります。SNSについては昨今、取扱いについて大変デリケートな面がありますが、私は会社のルールに基づき有効に活用しています。一つは数が増え続けるお客様の「見守り＝パトロール機能」で、例えば“お家を新築した” “二人目のお子様ができた” “お子様が大学に合格された” “息子さんが晴れて社会人となった”などお客様にとって何か嬉しいエピソードの投稿があった時には「おめでとうございます」などメッセージを入れます。仕事の合間におそらく１日２０件くらいはお客様の見守りパトロールは出来ていると思います。その中で「これは」という投稿に１日5件くらいでしょうか、コメントも入れています。時に深いお悩みを投稿していらっしゃる場合は「大丈夫ですか？」とお電話を入れたりもします。もう一つの機能は「岡崎まさみファン化機能」です。人は接触回数の多い人に好意を抱く傾向があると言います。私自身のFB投稿内容は個人がつける毎日の日記のようなものです。感動したことや感謝したこと、家族との時間、母の介護録、溺愛する甥っ子との休日エピソードなどを綴るのですが、これが岡崎まさみという人間を知ってもらうことに大いに役立っていると感じます。お客様から「岡崎さんの投稿を読むと元気になれます！ほっこりしました」「いつも気付きをいただいています、ありがとう」「本出してください、絶対読みます！」など良くコメントをいただきます。直接面談をしていなくても心の距離は近くなり、身近な担当者として認識くださっている実感があります。実は私の投稿によく登場する私の主人や父にはファンがたくさんいらっしゃいます（笑）。<br>「攻めの保全」については、まず先に挙げたお金の相談会。こちらは“新社会人向け” “新婚さん向け” “住宅購入者向け” “独立する人向け” “退職する方向け”その他介護、相続など人生の要所で知っておきたいお金の講座を10種類ほど用意しています。「2年ごとの連絡は私からしますが、お金のことで気になることがあって、ご興味のある講座を聞きたい時は支社の応接室でお話ししますからいつでもいらしてください。また私の講座は全部無料ですから、もし一緒に聞きたいご家族やご友人がいたら一緒にお越しいただいてもかまいません」とお伝えしています。これがタイムリーな保全と自然なご紹介にもつながるわけです。家を探している方同士で来てくれてもいい、ママ友同士で来てくれてもいい。支社に来ていただくこと、複数同時にお話することで時間の効率化も図れます。<br>また、私のお客様は女性で個人事業主、サロン経営などをしている方が多いのですが、<img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 my-4 my-lg-2" src="/images/interview/vol04_img01.jpg" alt="Special interview">私たちのように充実した社内研修があるわけでもなく、学ぶ機会が少ないと言われます。結果は出しつつも「自分のやり方が本当によいかどうか？」と迷いながら仕事をしていることが多いのです。そこで月に２～３度、少人数の座談会を開催しています。成功体験をシェアいただいたり、事業で困っていることに対して他の方とのアイデア交換をしたり、最後にネットワーキングタイムも設けています。つまり岡崎が担当になるともれなく、岡山の志をもって活躍する女性たちの上質なコミュニティーに参加出来るという付加価値を提供しているのです。ここもご紹介につながるポイントかもしれません。もちろん大阪や東京など遠方のお客様には私から出向くこともあります。</p>
