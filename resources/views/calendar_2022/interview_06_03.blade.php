          <h3>―― 私どものカレンダーに関して今後期待される事や、こんなものがあったらいいな、というご希望あればお聞かせ下さい。</h3>

          <p><span class="text-blue">井芹：</span>先ず、カレンダーはとても満足しています。私、カレンダーの機能で大切だなと思う事があって、それはお客様が日付欄に色々予定を書ける事。こちらのカレンダーは日付箇所にたくさん書ける事がすごく嬉しいです。もちろん併せて部屋に飾って綺麗というのが一番良いですね。実は御社のカレンダーを使わせていただく前に他社のものを使っていたのですが、何名かのお客様から「年末のカレンダーは使わないのでお断りしているんですよ」と言われた事がありました。という事で、その後はその方々にはお渡しする事を控えていたのですが、キューズプラスさんのカレンダーに切り替えた時、それをうっかり忘れてしまって、お届けしてしまったんですよ。<img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 my-4 my-lg-2" src="/images/interview/vol06_img03.jpg" alt="Special interview">そうしたら、そのお客様から「カレンダーの写真すごく綺麗で素敵ですね、ありがとうございます」と言われました(笑)。お断りされるどころか、わざわざそうやって連絡してきてくださったので、とても良かったと思いました。その時にですね、鎌谷さん(神戸第二支社/鎌谷 雅彦様)がおっしゃられていた、(カレンダーの)写真を通じてお客様とお話が出来るとおっしゃられたエピソードを思い出して、私が知っている写真のお話を差し上げると「そうなんですね」とおっしゃって下さってカレンダーをそのまま使っていただいています(笑)。<br>後は郵送の手間が無くなった事ですね。もともと毎年12月は本当に大変なのと、カレンダーはあの大きさが丁度良いのですが、あのサイズを安価に送る術が他には無くて。その辺、もう何もかも考えなくて良いので、本当に助かっています。岡崎さん(岡山中央支社/岡崎 まさみ様)から最初に「カレンダーを送ってくれて…」って話を聞いたんですね。「え！どういうこと？どういうこと？」ってなって(笑)。訳が判らずで(笑)。判って直ぐ「それ教えて！教えて！」から始まったんですよ (笑)。クオリティは素晴らしいしこんなに助かるものはないと思って本当に重宝しています。</p>

