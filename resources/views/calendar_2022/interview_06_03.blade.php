          <h3>―― 若い世代のLPの方々へメッセージと共に、保全活動とは？</h3>

          <p><span class="text-blue">井芹：</span>難しいですよね。ものすごく難しいですね(苦笑)。少し違うかも知れないのですが、保全活動では良く自分自身に愛称を付けていまして、私は「豊かさと財の女神」と自分で名乗っています(笑)。「私は豊かさと財の女神なので、私とお付き合いするとあなたの人生は100歳まで豊かになるし、お金に困る事もないです」と必ずお話をしています。保全とは、本当にその方の人生を豊かにしたいという思いでお付き合いするという事に尽きると私は思っていて、それに私のこの性格はスーパーポジティブなので、私と会ったら元気が貰えるとか、私と会ったら成長出来るとか、私と会ったらミラクルが起こったとか(笑)。そういう風にお客様に何かを与えられると言うとおこがましいですが、何かその時に一個でも良い事が起こるような事が出来る活動が出来たら良いなと思いますね。</p>
