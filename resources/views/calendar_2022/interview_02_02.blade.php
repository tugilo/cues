        <section class="interview-box">
          <h3>―― 保全活動を継続していく「コツ」があれば教えてください。</h3>
          <p><span class="text-blue">中村：</span>これは「コツ」ではなく、やはり「システム化」すべきものかと思います。お客様に対する「お客様のことを深く知りたい」という強い想いを根底に、活動をシステム化していく。コツで続けていけるものではないと思います。</p>

          <p><span class="text-blue">聞き手：</span>鎌谷さんのお話をお伺いした際に、「シンプルに続けられるものが重要」だと仰っておられました。無理をせず一度始めたらずっと続けられるものでなければ、と。</p>

          <p><span class="text-blue">中村：</span>その通りです。私も最初「かわら版」を作っていた頃、周囲から「継続性がなかったら途中でやめることになるので、逆にやらない方がいいよ。」と言われました。当時私の支社では、同じような取り組みをする人は誰も居ませんでした。私はただ、ご加入後のお客様になかなかお会いしに行けないことが申し訳ない、という気持ちで始めています。結局、周りがどう言おうが「自分がやるか、やらないか」だけの話です。もちろん、きちんと継続できるものでなければダメです。</p>

          <p><span class="text-blue">聞き手：</span>保全は、最初に「やる」と決めてしまうことが肝要、ということでしょうか？</p>

          <p><span class="text-blue">中村：</span>私の場合「お客様のことを深く知りたい」、これだけです。言い換えると、私がお客様のファンなわけです。お客様が私のファンではなく、その逆です。私はお客様のファンだから、お客様も私のファンになってくれます。まずは、私がお客様に共感します。私自身はお客様から共感してもらおうとか、ファンになってもらおうとは思っていません。もし、ファンになってもらおうと思うのであれば、これ見よがしに自慢するようなことはしないですよね？多分、謙虚に振る舞うことになると思います。共感されるのではなく、まず私がお客様に共感する。だから私は、「中村さんって謙虚な人だな。」と思われようとは考えないのです。まず私がお客様のファンになって、それからお客様も「頑張っているな。」「いつも自慢新聞送ってくるけど、ちゃんとやることはやってくれているな。」と、そのように共感してくれるかどうかは分かりませんが、そうあるべきだと思っています。私がお客様のファンなのです。最初からファンにならないとダメだと思います。</p>

          <p><span class="text-blue">聞き手：</span>それはすごい…。つまり、中村さんがお客様のファンだからこそ、「お届けもの」も多くなるみたいなイメージですね。</p>

          <p><span class="text-blue">中村：</span>そういうことです。お客様が私のファンだから、と思うなら何もしなくていいと思います。保全は、お客様が喜ぶというよりも、どちらかというと私のほうが喜んでいます。いわば与える側の方が、喜びが深いわけです。人の役に立ちたいという、人間の根本的な欲求ですね。人の役に立っていることがわかると、自分の存在意義や価値が見出せます。私たちの仕事は、断られたり電話に出てもらえなかったりとか「自分の存在意義って何だろうな。」と思う時があります。しかし、バースデーコールや通信物をお送りすると、すぐに効果はわかりにくいものの年を追うごとに「毎年ありがとうね」と喜んでいただけます。それは自分の存在意義を明確に見出すことでもあるのです。</p>

          <p><span class="text-blue">聞き手：</span>新規契約の活動が忙しくて保全活動に時間を割けないという声を時々伺うことがありますが、仮にこのようなことを後輩の方々から相談された場合、中村さんはどのようなアドバイスをしますか？</p>

          <p><span class="text-blue">中村：</span>担当者としての意識を再確認して、いいものを勧めたらそれで終わりではなく、保険という商品とライフプランナーが一体化して成立するわけだから「お前、顔忘れられてないか？」とか「担当者の存在がお客様に浸透しているか？」ということを問うと思います。</p>
