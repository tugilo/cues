        <section class="interview-box">
          <h3>―― プルマサニュース以外でお客様への定期的なコンタクトはお持ちですか？</h3>
          <p><span class="text-blue">中村：</span>それはバースデーコールです。年に一回はお客様の声を聞く。どういう状況になっているのか、どういう環境になっているのかを。昨年と現在のお客様の状況がどのように変化しているかを把握するには、バースデーコールが一番確実です。実は私も10年間はバースデーカードをお送りしていましたが、一方通行だと思うのです。その点、バースデーコールは所謂、双方向性といいますか、私が電話したら言葉が返ってきます。それは保険を見直したい、解約したい、あるいは追加で入りたい、紹介したい人がいるとか子どもが成長したとか、社会人になったとか退職したとかいろいろな情報、いい情報も悪い情報も生で聞こえる、その場で聞き取ることができるので、そういうことをしなければと入社10年を過ぎた頃から思いました。<br>今ではカスタマーサービスセンターがあるので直接そこに連絡を入れられるケースもありますが、当時は担当者としてカスタマーサービスセンターじゃなくて自分に言ってきてもらったらいいのに、ということも思いました。そこからバースデーコールにしよう、お客様の生の声を聞くようにしよう、となりました。</p>

          <p><span class="text-blue">聞き手：</span>バースデーコールのほか、さきほどお話を伺ったおハガキ以外に何かありますか？</p>

          <p><span class="text-blue">中村：</span>外部業者が発行している情報はがきみたいなものを、全員ではなく対象を限定して、法人経営者もしくは士業の方にお送りしています。この人はどうされているのだろう？と電話するのもいいのですが、相手やその時に応じて電話ではなくはがきでコンタクトを取ることもあります。</p>
        </section>
        <!-- n:interview-box -->

        <!-- s:interview-box -->
        <section class="interview-box">
          <h3>―― 中村さんはライフプランナーとして大変長いキャリアと実績を共にお持ちですが、保全に関する具体的な取り組み方は最初から変わりませんか？また、お客様はどのような関わり方を期待されているとお感じですか？</h3>
          <p><span class="text-blue">中村：</span>これは変わらないです。担当者として当事者意識というものを強く持っています。「自分が担当者だ」ということです。もっと大きな枠で考えると、生命保険業界40数社ある中でプルデンシャル生命と出会い、中村将幸と出会って、そして私に保障を任せてもらえている、という感謝の念が根底にあります。ですので、今も昔も保全に対する姿勢は変わらないです。<br>またお客様の期待値ですが、これは「契約後の年数」によって様々かと思います。仮にご加入後1～2年といったお客様であれば、契約内容を確認される方はあまりおられません。しかし、お客様のお仕事に関するご相談など、保険以外の情報提供についてご要望をいただくことはあります。これは、バースデーコールやアンケートはがきがきっかけとなることが多いです。<img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 my-4 my-lg-2" src="/images/interview/vol02_img02.jpg" alt="Special interview">相応の年数が経過したお客様には、「契約内容の見直しを」と私から促すこともありますが、ほとんどの場合、お客様からお申し出いただくことが多いです。アンケートにお答えいただいたご要望に沿って、追加のご契約をお預かりすることも多々あります。</p>

          <p><span class="text-blue">聞き手：</span>私どものアンケートに、具体的なご要望事項をお書きいただけるということですか？</p>

          <p><span class="text-blue">中村：</span>はい。子どもさんの学費、年金や介護に関するご相談など、お客様のご要望を汲み取るうえでアンケートは非常に役立っています。それだけに、このアンケートの質問項目は毎年同じものではなく、その時その時の旬を意識して、常にお客様にご興味をもっていただけるような工夫を凝らすことが大切だと思います。</p>
        </section>
        <!-- n:interview-box -->

