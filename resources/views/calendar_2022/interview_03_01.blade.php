      <section class="interview-box">
        <h3>―― 最初に、田内さんが普段、取り組んでおられる保全活動について教えてください。</h3>
        <p><span class="text-blue">田内：</span>実際、私が行っている保全として主なものは、お客様には必ず誕生日に電話をしている事です。「それって保全なの？」と思われるかも知れませんが、10年以上続けていると思います。世帯の方であれば、ご契約者はもちろん、保険に加入していなくても、その配偶者の方の誕生日にも電話をします。世帯なら年に2回はお電話を差し上げます。独身の方なら年に1回ですね。それと、海外からエアメールをお客様と見込客の両方に送っています。もちろん販促の意味合いも兼ねてです。今年は新型コロナウイルスの影響で中止が相次いでいますが、今まではPTC、PIIC、MDRT世界大会とTOT会議といった会社や業界の海外のスケジュールに、少なくとも年4回は出席します。その中でハワイのような遊び色のイメージが強い場所は避けますが、滞在先からエアメールを送ります。<br>普通のご家庭にはエアメールが届く事は余りないと思います。もちろんお客様によって受け止め方はそれぞれかと思いますが、もしエアメールが年2回届いたら、「エアメールが届くんだ」ときっと特別に思っていただけるはずです。このエアメールは10年近くやっていると思います。お客様からすると、「この保険のセールスパーソンは普段は保険を売っているけど、いつも海外に情報を取りに行ったり、米国の本社から呼ばれたりいろいろと忙しくしている。この人はそんな人なんだな」というイメージを持たれると思います。つまり、仮に何となくでも「普通ならコンタクトを取りにくい頑張り屋さんが担当になってくれている」という感覚を持ってもらっていると思います。<br>私の取り組みが保全にどのようにつながるのかという点では、お客様とちょうどよい距離感を保っているというところが大事な点と考えています。毎年毎年ワンパターンですみませんという感じですが、電話に出た瞬間に「毎年ありがとう」から始まる方、「もうええよ」と言われる方、反応は様々です。どちらかというと、私は「お詫び行脚」みたいな感じ (笑)。行脚は出来ていないけれども「毎年ワンパターンですみません。会いに行くと言いながら、会えたらいいですね」と言いながら、「全然お会い出来てないのですけど、お変わりないですか？」という感じです。<br>話は少し戻りますが、ある意味マニアックな方は、私のエアメールを全部置いてくれている人もおられます。別の話では、毎年の年末にキューズプラスさんのカレンダーと共にお送りする「今年もありがとうございました」の通信物も、全部手元に置いてくれている人もいらっしゃって、それこそファイルに綴じてくださっている方もおられます。</p>

        <p><span class="text-blue">聞き手：</span>カレンダーに同封される年末の「お客様レター」ですね。あちらは本当に田内さんのお気持ちが表れていますよね。A4用紙2～3枚分表裏を文字ぎっしりで書いてらっしゃいますが、紙面ではほぼお仕事の話は触れていらっしゃいませんよね。本当に身の回りに起きた事、感じている事を書いているだけで、親近感が湧く方が多いかと思います。</p>

        <p><span class="text-blue">田内：</span>バースデーコールをしていると、11月や12月ぐらいに電話する方から「また年末のレポートを楽しみにしています」と言われますし、年が明けて1～2月頃のお誕生日の方から大体、「レポートを読みました」とか「お嬢さんが…」とか「子どもが…」とか「ご長男が…」とか、そんな質問をされる事があります。多分「田内さんは海外に行くとか、ばりばり仕事している人だね」と、お客様には映っているように思います。もちろんそう言う風に普段から自分を表現しているところもあるのですが、このレポートでは人間的な視点というか、あくまで普通の家庭のお父さんお母さんが経験するような事を普通の目線で話を綴っています。「そうそう。そういう事よね」みたいな「自分が思っていた事って、こういう事だよね」とか。「そういう事って、つい忘れがちだよね」と言う共感をしてもらえたらと思うし、「田内さんもそういうトラップに嵌ったの？」ではありませんが、お客様にとって「そうだな！」という何かしらの気付きや共感をしてもらえるようなことが伝わるといいな、と思いながら書いています。(仕事の事は)その昔は書いていましたが、結局、お客様の共感が得られるかというと、全然そうではありませんでした。僕も失敗しながら気付いていくのですが、やはり営業の世界に入って、フルコミでMDRTに入会しているとかとなると、気持ち「どうだ？俺様は！」となってしまいがちだと思います。実際はフルコミなので報酬がデコボコ、まるでジェットコースターのようで、とてもゆとりがある時もあれば、それこそ社会保険料が支払えないぐらいになる事もありますけどね。結果を出していると「俺様ってすごいだろう」という事で「いい仕事をするし、誰か紹介してよ」ってなりがちなんですけれども、そうではないと思います。保険の仕事なので「いつもとは言わないけども、心の距離感は常に傍にいてくれてる」みたいな事が重要ではないかと思います。私はお客様のご自宅の電話も携帯電話も全部登録していて、電話に出る時は「井手上さん、こんにちは！」となるのか「山田さん、田内です！」とか、相手のお名前をお呼びして出ています。工夫という事ではないですが、電話に出る時にお名前をお呼びして出る方が相手の方も気持ちいいだろうなと思うからです。私の場合はそんな距離感の作り方を大切にしています。</p>

        <p><span class="text-blue">聞き手：</span>いつも大切にされておられる事は、今述べられた「距離感」なのでしょうね。他にはいかがですか？</p>

        <p><span class="text-blue">田内：</span>そうですね。私たちPOJは家族愛を謳っている会社なので、家族との出来事みたいなものは入れたいなとは思っています。ただ、それが親バカみたいな表現にならないように気は遣います。どこの家庭でもありそうな事だけども、身内の話を書くようにしています。良い事、悪い事、子どもと喧嘩した事とか。今年は自分が老害になっているなという話も書こうかなと思っていたのですが(笑)。家族とのそういう話題は、最近のレポートのテーマでもあるんですけど、それと「なんとなく日々の生活の中で感じている事」、「大切にしている事」を知ってもらいたいな、という思いを込めて書こうと思っています。今は情報が溢れかえっている時代なので、ついつい情報に振り回されてしまいやすい。自分とは異なる物の見方もありますから、深く検知・見聞を広げる事についても、重要なテーマになると思っていますが、価値があるとか意味があるとか効果があるとか、或いは得する、損するとか、そういう話題ではなくて、<img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 my-4 my-lg-2" src="/images/interview/vol03_img01.jpg" alt="Special interview">なんとなく自分が日々の生活の中で感じている事、大切にしている事を共有というと押しつけがましいのですが、お客様に知ってもらいたいなと思っています。<br>そう、面白い事にそのレポートが紹介につながるケースもあります。例えば、徳島のある会社の経営者の方が「アレを頂戴」とおっしゃられたのでお渡しすると、「こんなの書いている人がいるのよ。実はうちの会社を担当してくれている保険の人なんだけど」と、徳島のお菓子屋さんで一番大きな法人様につなげて下さったり。他にも兵庫県で社員380人ぐらいの医療器具卸の会社に繋いでいただいたりという事もあるので保全しつつも、グイグイとは行っていないけれども見込み客発見につながったりしています。</p>
