          <h3>―― 金指さんが17年間お勤めになられ、長くキャリアを積まれて良かったと思うところを教えてください。また、後進の方や、特に女性LPの方々にお伝え出来る事があればお願いします。</h3>

          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol08_img03.jpg" alt="Special interview">
          <p><span class="text-blue">金指：</span>いろいろありますが、私が一番好きなのがFree to Workですね。産休を取りやすいというのもありますが、特に今、子どもがいて例えば、「授業参観が入った」とか、「運動会が入った」とか、何かあった時にも自分の生活に仕事を合わせられるというところがとても有難いですね。LPであればその辺は自由で、自分の時間の中でこの時間は働く、この時間は子どもといるという事を自分で選べます。私も前職だったら絶対に出来なかったと思うので、そういうところはとても良かったと思っています。<br>他にはお客様とずっと15年とか17年間ずっと人生を共に歩めるというのも素晴らしい事で、やりがいのある仕事だなと思います。<br>LPになって本当に良かったと思います。</p>

          <p><span class="text-blue">聞き手：</span>最後に、LPとしてキャリアを積まれて、金指さんご自身が成長されたと感じる部分はございますか？</p>

          <p><span class="text-blue">金指：</span>私の成長ですか？なんでしょうね。成長しているかな…(笑)。そうですね。前職との比較になりますが、昔は担当として割り当てられたお客様があって、いわばルートセールスのような仕事でした。その中でお客様を温めながら数字を作っていくという感じでしたが、今のLPの仕事は予め与えられたお客様はいらっしゃいません。自分でお客様を作らなければなりませんから、そのためにも自分がこの仕事を通して何を伝えたいのか、何をやりたいのか？、どんな方と同じ時間を過ごしていきたいのか？どんな人生を歩んでいきたいのか？などを考えるようになりました。<br>
          LPになる前は、こういう事を考える事は先ずありませんでした。今私がきちんと出来ているかどうかは分かりませんが、少なくとも考えて実践しようとする自分はいます。もしかすると、これが私がLPという仕事を通じて得られた成長、という事になるかもしれません。</p>

          <p><span class="text-blue">聞き手：</span>今日の金指さんのお話を伺って、LPとして、普段の何気ない取り組みのように見える部分にも、実は本当に深いところで大切にされている想いが込められている事が今日のインタビューを通じて、気がついたというか感じたというか。私はそんな気がします。本日はありがとうございました。</p>

