      <section class="interview-box">
        <h3>―― お客様はご加入後、ライフプランナーの皆様にどのような関わり方を期待されていると思われますか？</h3>
        <p><span class="text-blue">鎌谷：</span>私が思うには、お客様は「常に傍にいて欲しい」と思っていらっしゃいますし、「何かあれば相談したい」と思ってご加入いただいていると思います。しかし、どうしても物理的に無理な部分があります。「お客様の望み」と「私たちの現実」が乖離しているとクレームになるわけです。そんなことにならないように、私は契約時に必ずこのような話をするようにしています。『実は、私は2000数百人のお客様を担当させていただいております。例えば300人のお客様を担当していて3年に1回新しい保険にリニューアルしていただき、そこから再び手数料がもらえるのであれば私は300人でいいのですが、私たちはそれをしない会社です。必要性があれば増やすし、なければ減らすし、何も変わっていなければそのままで、というチェックはさせていただきます。ただ、2000数百人のお客様を毎年必ず回っていくことは物理的に無理だと思うので、私から「お変わりないですか？」「会いに行きましょうか？」と発信させていただきますので、申し訳ないのですが必ずそれに答えてもらえませんか？』と、こんな感じでお客様に余り良いことばかりをお伝えせず、現実との乖離が無いようにしています。</p>

        <p><span class="text-blue">聞き手：</span>お客様には今こういうお客様がたくさんいらっしゃいますので、何かあったらお声かけをいただきたいことを予めお伝えしておくということですね。その部分をもう少し具体的に(お客様にお伝えしていることを)教えていただけますか？</p>

        <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol01_img02.jpg" alt="Special interview">

        <p><span class="text-blue">鎌谷：</span>そうですね、「アンケートを出しますので、ご返信いただけるとありがたいです。急ぎのことに関してはすぐにお電話ください」というお願いをしています。他には、先ほどと少し重なりますが、「その時には必ずしっかりやります。ただ、私から定期的に毎年ご訪問というのは物理的に無理な状態なのです。それはなぜかというと、こういう形なので新契約がないと会社に居られなくなって、私はお客様の担当もやめないといけなくなる。そのバランスなのです。」という話をします。契約の時から、お客様に保全の在り方をご理解いただけるようお話しします。それには価値観の合うお客様を選ぶということがすごく大事になります。<br>ご紹介については、紹介をする方の価値観や責任感が同じような方をご紹介いただくことがポイントかと思われます。そうじゃないと「何言ってるのお前？」「こっちは金を払ってるんだぞ」という話になる。そういう人をお客様にしてしまうと、多くの時間が独り占めされてしまいますので、他のお客様にも迷惑がかかってしまいます。ですから「ああ、そうだよね」「こちらから連絡したらすぐ来てね」みたいな感じのお客様が出来るだけ増えていただけるようにと思って仕事をしています。</p>

        <p><span class="text-blue">聞き手：</span>お客様とライフプランナー様との価値観が一緒でなければいけないということですね。</p>


        <p><span class="text-blue">鎌谷：</span>そうですね。私達はものを売る仕事なのですが、形のあるものを売っていないので「これどうですか？これすごくかっこいいでしょう」と言った時の反応ではなく、ご自身がどう考えて家族をどう守りたいかとか、どういう価値観でプルデンシャルだったら加入してもいいかなとか、サインする前に営業マンだったらおおよそ判ると思うのです。やっぱり、そこがすごく大事なので。だから、ご紹介もあなたがもしお友達に何かあった時に心が痛む方だけを紹介してくださいとお願いしているのです。そうすると同じ価値観を持った仲のいいお客様がまた増えていくことになります。それが後々適切な保全につながる大きなポイントだと思うのです。もし、そうではないお客様ばかりになってしまったら新規契約の時間が無くなって、おそらく保全99対 新規契約1ぐらいになってしまうでしょうね。しかも、継続率も悪いでしょうし。</p>

        <p><span class="text-blue">聞き手：</span>お客様にこちらの価値観を意識付けしたり、意識を高めるということは？</p>

        <p><span class="text-blue">鎌谷：</span>いや、それは難しいと思いますよ。というよりもむしろ「もともとお持ちの方」にお会いしなければいけないのかなと。人の意識は簡単には変わらないので。「この方は少し違うかな」と思った時には次のアポを取らないで、「お気持ちが向いたらまたお願いします」と言って帰るようにしています。</p>

        <p><span class="text-blue">聞き手：</span>今のお話は個人のお客様だと思いますが、法人のお客様にはいかがでしょうか？</p>

        <p><span class="text-blue">鎌谷：</span>私の個人のお客様は、経営者やドクターというお立場の方が多いのですが、その方々から法人のお客様をご紹介いただく際には、先ず法人のご提案の前に、奥さまにもご同席をいただいて、ご家族の保険のお話をさせていただけるようにお願いをしています。社長は個人で家族をどう思うか？という気持ちを奥様と一緒に共有していただいて、その中で信頼を得た上で次に法人のご提案を行うようにしています。</p>
      </section>
