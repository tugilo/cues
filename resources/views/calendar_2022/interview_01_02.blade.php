        <h3>―― 保全活動を継続していくためのコツはいかがでしょうか？</h3>
        <p><span class="text-blue">鎌谷：</span>そうですね。無理をしてたくさんの保全はしない。継続できる自分の範疇をしっかり見極めた上で始めるということです。さらに、手がかからず、親近感が湧くものでないとダメなので、バースデーカードにしたら宛名だけ手書きにするとか暑中見舞いとか年賀状にしたら裏側にこだわるとかですね。<br>カレンダーについては、なるべく使いやすく喜んでいただけるようなものを送りたい。でも、すべて自分で手作りするというのは難しいので（笑）井手上さんにお願いしています。手を抜けるところと、かけるところを上手くやっていけば良いのではと思います。<br>日々まだまだ忙しい若いライフプランナーへのアドバイスとなると、そうですね、社歴の浅い方は「新規の獲得をするための保全」という意識でやっていけばいいのではと思いますね。先ほど、少し触れましたが保全というアフターフォローをしていると、そこから紹介をいただけることがあるので、その紹介が新規の契約に繋がっていくことになります。保全により紹介を広げることが出来れば、新規の契約も増え、有り難い訳です。新規の契約に時間を取られている状態は紹介があるからという事ですよね。言うなれば保全から新規でしょうか。もちろん保全から解約もありますけど（苦笑）。</p>
      </section>
