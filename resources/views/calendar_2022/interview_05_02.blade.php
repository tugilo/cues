          <h3>―― お客様はご契約後、LPの皆様にどのような関わりを期待されていらっしゃると思いますか？</h3>

          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol05_img02.jpg" alt="Special interview">
          <p><span class="text-blue">長曽我部：</span>一般的な個人のお客様に関しては、先程お話したような形で、常に近くで感じてもらえるような投げかけを継続する事でご安心いただけるのではないかと思います。一方で経営者の方々と言うのは、もしかしたら少し違うかもしれません。<br>やっている事は当然同じなのですが、私たちは「生命保険のプロ」という言葉をよく使います。お客様に対して「プロがずっとお世話させていただきますよ。」とお伝えする事がありますが、それを発信している以上は保険の入口から出口まで、その間に必要とされる保険に関する様々な事は出来る事が求められます。もっと言えば、LPは他社が出来ない事も含めて出来て当然なのです。「出来て当たり前」という事を先ずお感じいただいた上で、お互いが保険の事を話題に出さなくても良い関係を作っていきたい。これはなかなか難しい事で、私もまだまだ出来ていないかもしれません。少々乱暴な表現かもしれませんが「ご契約してくださった方は、全員友達」のようなイメージでお客様が思ってくださるのなら最高です。もちろんそうは言っても、「担当LP」としての立場がありますので、ご契約をお預かりした上に「お前、明日から友達な」とは当然出来ません。しかし、お客様の方が「こいつはうちの担当LPだけど友達だ」と思って下さればベストという事です。もしそのように思っていただけるようなコミュニティが出来上がれば、自ずと新規契約も保全も同時進行で出来ます。なかなかこれは難しい事ですが、あれやったり、これやったり、なんとなく色々な事をするよりも、普段から共通の事で接する事が出来たら一番の保全活動にならないかなと今も思っています。このように思うようになったのは、ちょうど10年ぐらい前からで、入社後私は6年半をLP、その後2003～2007年の4年間はマネージャーとして仕事をしていました。その後、営業所長(以降SM)からLPに職種変更して新たにリスタートするにあたって、今後どうやっていこうかと考えた事がきっかけです。離れていったお客様もいらっしゃいますが、過去からの流れで2～3年間はLPとしての仕事自体は何ともありませんでした。しかし、今後もLPをずっと続けていく中で、このままではいけないな、どうすれば良いかなと色々考え、「こうありたい」というイメージから今の流れになっていった感じです。</p>

          <p><span class="text-blue">聞き手：</span>長曽我部さんが「こうありたいな」というイメージが先にあったという事ですね？それはSMになる前のLP時代とは明らかに違うのですか？</p>

          <p><span class="text-blue">長曽我部：</span>明らかに違いますね。SMをしているとLPは売れなくなったり調子が悪くなると、なんとなく卑屈になるというか、気持ちが荒んでくるところがあると感じました。自分が採用したLPを介して経験した事なのですが、周りが悪いと考えるようになってしまうのです。自分が悪いのではなく周りが悪い。判ってくれないお客様が悪いとか。ご紹介くれない人が悪いとか。でも、よくよく考えたら、きっとその方とは合わなかっただけなのです。残念ながら私どもがアプローチしてもご契約いただけなかったというのは、一言で言ってしまえばご縁がなかった。きっと内容云々よりも、その時の感覚として「この人とは合わないな」とお互いが感じたのだと思います。マネージャーからLPに職種変更をしてから少し経った時、それを出来るだけ無くしていきたいと思ったのです。判り易い表現をすると「楽なお客様と仕事がしたい」。楽というのは楽をするという意味ではなく、価値観を共有出来るお客様と一緒に成長していきたいなと思ったのです。誰でも彼でもと自分の土俵ではないところに行くと、パチンと叩かれてご契約をお預かり出来なくて卑屈になって…という連鎖が起こるのだろうと思います。ですから、自分の土俵を上手く見つけていく仕事でもあるのかなと思います。</p>
        </section>
