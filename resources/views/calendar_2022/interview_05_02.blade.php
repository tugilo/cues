        <section class="interview-box">
          <h3>―― 保全活動を継続していく為のコツをお願いします。</h3>

          <p><span class="text-blue">長曽我部：</span>続けられる事を続ける、無理をしないという事です。これは保全だけに限りません。とかくあっちの水は甘く見え、隣の芝は綺麗に見えるものです。だから、ついつい人の話を聞いてしまうと「俺もこんな事をやってみよう」と思ってしまう。新契約もそうです。誰かがこういう事で成功したと言うと「俺もそれやってみよう」となってしまうのがLPの性なのですが、先ずは最初の段階で見極めないといけません。「自分に出来るのかどうか」という事と、「自分に合っているのかどうなのか」という事を。合っていなかったら絶対に続けられないですから、続けられない事は最初からしない。それよりも究極の保全というのは、自分がそこにいる事。「We’ll be there, when you need us most.」というこの言葉に尽きると思うのです。</p>

          <p><span class="text-blue">聞き手：</span>「新契約の活動が忙しくて保全活動にはなかなか時間が割けない」と長曽我部さんがご相談を受けた場合、どのようなアドバイスをされますか？</p>

          <p><span class="text-blue">長曽我部：</span>これはもうまさに、「保全の目的をどう捉えるか」だけであると思います。当然の事ながら、さっきお話しした「保証」と「保障」の話の「ごんべんくん」。つまり「保証」は我々がLPである以上やっていかなければならないマストな事で誰でも出来て当たり前。と言う事は、「何かあった時にはそこにいるよ」とお客様に思っていただければよいのです。一方で、保全を特別な活動と捉えてやってしまうから、なかなか時間が割けないと思ってしまう訳です。であれば新契約と一緒にすればいいじゃないかと言うのが、僕が導いた簡単な解かなと思っています。他方LPは皆、保全活動してお客様をお守りする事とは別に、ご紹介もいただきたい訳です。その「ご紹介をいただく」、「新規顧客を見つける」という事は保全活動の成果ではなく、そもそも新契約における前段階、一連のセールスプロセスの「いの一番」だと思います。そこをシステマティックとまでは言いませんが、いわゆる保全活動と合体で既存のお客様と一緒にマーケティングを考えてみたらどうか？というアドバイスをすると思います。</p>
        </section>
