      <section class="interview-box">
        <h3>―― 金指さんはLPとして17年の長いキャリアをお持ちですが、保全に関する具体的な取り組みは、キャリアを積まれていくうちに変化はありましたか？</h3>

        <p><span class="text-blue">金指：</span>私の場合、特に変化していないと思います。カレンダーと年賀状のみお送りしており、これは入社直後からずっと取り組んでいる事で、変わっていません。</p>

        <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol08_img01.jpg" alt="Special interview">

        <p><span class="text-blue">聞き手：</span>敢えて変えていない、という事でしょうか。</p>

        <p><span class="text-blue">金指：</span>敢えて変えてない、というほどではないのですが、続けられるものを続けている、という感じです。</p>

        <p><span class="text-blue">聞き手：</span>話題が少し横道に逸れますが、従来お使いのカレンダーから、私どものカレンダーに替えていただいた決め手は、どのようなポイントだったのですか？</p>

        <p><span class="text-blue">金指：</span>まず、発送作業をお任せ出来るところですね。以前利用していたカレンダーでは自身で発送作業を行っていたので、毎年末とても大変でした。今では(発送を全て)お任せ出来るので、その分、年賀状の作成に時間が取れる。それが一番大きいですね。それにお客様アンケートを付けていただいている点も、付加価値としてとても有り難いです。お客様と双方向のやり取りが出来ますから。<br>
        先程お話ししたように、年賀状とカレンダーという取り組みの内容は変わっていませんが、給付金請求や保障内容の見直しなど、保全手続きの対応に掛ける時間は増えています。LPを17年続けていると、私も歳を取りますし、当然お客様もお歳を重ねていかれます。昔は給付金請求のお手続きはほとんどなかったわけですが、1回目の育休の時よりも、3回目の育休の方が営業所長に対応してもらった給付金請求手続きの件数は断然増えています。入社直後はそれこそ100％の時間配分で新規契約でしたが、時の経過とともに保全に要する時間は自然と多くなります。私は普段、あまり頻繁にお客様への能動的なアクションができていないので、お客様から何らかのアクションを起こしてくださる機会を大切にしようと思っています。そのうえでお申し出事項には迅速に対応出来るよう心掛けています。<br>毎年、お客様向けの冊子「Love & Trust」 が会社から送られるのですが、そこにもアンケートみたいなものが付いていて、LPの満足度などを書いて下さる項目があります。以前、私の対応について、「過剰でもなく不足でもなく、必要十分な対応をしてくれる」と、そんなコメントをあるお客様がして下さいました。私にとってとても嬉しいコメントで印象に残っています。「良い距離感」を保てていたのかなと。</p>
      </section>
