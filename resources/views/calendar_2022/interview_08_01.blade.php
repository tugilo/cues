      <section class="interview-box">
        <h3>―― まず、金指さんが普段取り組んでいらっしゃる保全活動について伺いたいのですが</h3>

        <p><span class="text-blue">金指：</span>入社後早いタイミングでお客様には毎年カレンダーを送る事を継続しています。その時は他社さんのカレンダーを購入していて、毎年自分で作業して送るのが結構大変だった記憶があります。今は御社のカレンダーを利用しています。</p>

        <p><span class="text-blue">聞き手：</span>金指さんは当社のカレンダーをもう6年間お使いいただいていますね。</p>

        <p><span class="text-blue">金指：</span>そうですね。随分と事務負担が軽減出来、助かっています。カレンダーの他には、これも入社以来継続しているのですが、年賀状ですね。毎年自分の家族の写真を載せています。これが保全活動にあたるかどうかは分かりませんが、子どもが生まれる前は夫と2人で、産後は子供とも一緒の写真を載せてお送りしています。また私は入社後に産休・育休を取らせてもらったことがあるのですが、その際に会社からお客様に産休・育休の案内は送ってくれるのですが、産休に入る時にいきなり、「しばらく休みます」と言うのも失礼だな…と思い、妊娠の報告とか、そろそろ休みます、のようなご連絡を当時、事前にしましたね。お腹が大きくなった写真とかもお送りして。それを送る事で、「出産後もちゃんと仕事をするよ」という意思表示になったのではないかと思います。<br>
        年賀状をお送りする際には、PCSのメモを確認しながら、「今年お客様とどんな事を話したかな？」と振り返りながら、また年賀状をお返しいただけるお客様もいらっしゃるので、いただいた年賀状も見ながら、必ずそのお客様ごとに一言メッセージを添えるようにしています。<br>
        あと、EMSSを活用して毎月メール配信をしています。新しくご契約いただいたお客様にもきっちりと送られるように、いつも送信対象のチェックは欠かさないようにしています。EMSSは会社が用意する定型文もあるのですが、お客様に何か新しい情報を発信したいときには、会社の事前確認を受けたうえで自作メッセージを配信するようにしています。</p>

        <p><span class="text-blue">聞き手：</span>年末年始にカレンダーと年賀状をお送りして、毎月の取り組みでEMSSがある、という事ですね。</p>

        <p><span class="text-blue">金指：</span>はい。ただ、EMSSはメールアドレスをご登録してくださっているお客様だけになります。ライフプランナー(以降 LP)初期の頃のお客様はちょっと漏れているかもしれませんが、最近のお客様には新しいご契約時にメールアドレスを必ずお知らせいただいているので、ほぼ全員に送れていると思っています。</p>
          <p><span class="text-blue">聞き手：</span>メール配信と手紙・ハガキの効果と申しますか、ハガキは見ていただける確率が比較的高いと思うのですが、メールについてお客様からの反応はいかがでしょうか？</p>

        <p><span class="text-blue">金指：</span>頻繁にリアクションがある訳ではないですが、ふとした事で、「そういえば、金指さんに連絡する事があったんだけど」みたいな感じで、EMSSメールにそのままご返信されるかたちで、「保険の見直しをしたいんですが」とか、「ちょっと判らない事があります」とか、そういうご連絡をたまにいただきます。月に1件でも2件でも何かリアクションがあると嬉しいですし、お客様もいちいち私のメールアドレスを名刺から探して返信する手間もなく、リアクションしたい時に手軽に返信できていいのではないかと思っています。。</p>

        <p><span class="text-blue">聞き手：</span>弊社カレンダーをお使いの方の中には、その後、年賀状をお止めになる方が一定割合いらっしゃる印象があります。金指さんはカレンダーと一緒に年賀状を続けておられますね。年賀状を続けられる理由が何かおありですか？</p>

        <p><span class="text-blue">金指：</span>そうですね。年賀状をお送りすると、中には返信して下さるお客様もいらして、そうするとお客様のご家族構成が変わっていたり、引っ越しされていたり、様々な情報が得られる事があります。何度か年賀状を今年で止めたいな、と思う事もありました(苦笑)が、お客様の変化を感じられるコミュニケーション手段のひとつではないかと考え続けています。<br>あと、年賀状に載せる写真で私の顔を見ていただける機会になっているのも続ける目的のひとつかなと。久しぶりにお会いしたお客様からは、「いつも年賀状で見ているから金指さんの顔は忘れないよ」とか、「年賀状で見ているから久しぶりな感じがしないね」とか、そうおっしゃっていただく方が結構多いんです。お客様全員とはなかなか頻繁にはお会い出来ませんから、こうして自分の顔を見てもらえるのは意味があると思っています。</p>

        <p><span class="text-blue">聞き手：</span>金指さんの近況をお伝えし、お客様の状況を把握するための手段のひとつとして意義がある、という事ですね。</p>

        <p><span class="text-blue">金指：</span>そうですね。</p>
      </section>
