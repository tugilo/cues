        <h3>―― リリースから開発をずっと鎌谷さんとご一緒いただいているカレンダーですが、お役に立っている点と、改めて今後、期待される点をお願いします。</h3>
        <p><span class="text-blue">鎌谷：</span>カレンダーの必要性があるお家の中では「予定を書き込む」という作業があると思うので、書き込むためにペンの滑りが良すぎると困ります。その為にちょっとざらついた感じの紙質がいいかな、という点など紙の仕様や暦部を大きく取るようにお願いしました。<br>そして、暦の下には常に会社のCS連絡先とプルデンシャルのロゴが描かれているという形をお願いしました。特にカレンダーはご家庭で見ていただく機会が大変多いですから、なによりもお部屋に飾って、まるで絵画のように、カッコ良い「絵になる写真」であることが一番の望みでした。プルデンシャルの親会社はアメリカなので、アメリカの風景を主体にカレンダーを作ってほしいという事でしたね。<br>また、仮に普段カレンダーを使わない方にも、担当者がお客様のご家庭のために何らかのアクションを起こそうとしている姿勢が伝わるよう簡単に返信できるアンケートもセットにしてもらいました。<br>実際、私が一年間でお客様にお送りしている通信物の中で、カレンダーは最も重要なツールとなり、仮にカレンダーがご不要で捨てられたとしても、アンケートにはお答えいただける可能性があります。カレンダーのクオリティは、大変評価が高く、保険を解約された方でも「あのカレンダーはどこで手に入るのか？」と聞かれるくらいです（笑）。それで、「もう一度加入いただけるのであればお渡しできます。」とメールに書いたのですが（笑）。<br>それに配送システムがいい。井手上さんにご提案いただいた「ゆうメール」を使っているということ。他の宅配業者の配送方法であれば、転居されていると返ってきてしまう。過去、配送中にボロボロになったものが返ってきて、何回かやり取りしているうちに傷んだカレンダーがお客様に届けられるという事があり、現在の不着が非常に少ない配送方法は大きなポイントです。<br>また私は過去、臨時でアシスタント登録した娘と家内がブースにこもって３人で作業をし、やっと発送するみたいな事を続けていました。その間は一切営業ができない状態です。年末に大事なお客様のお顔を見ることができないというデメリットが多くありました。でも今は、時間確保が必ずでき、年末のご挨拶にしっかり行けるようになったメリットは非常に大きいと思います。<br>このカレンダーは井手上さんにいろいろ考えていただいて、利便性、封入・発送作業の時間の節約、八雲さんの「カッコ良い絵」になる写真、お客様との関係を深めるアンケートなど、細部までコンセプトが具現化されており、非常にいい形になっていると思います。ライフプランナーにとって、年末の時間の有意義な使い方が、このカレンダーで実現できるのではないかと思います。もう既に非常にレベルが高い状態でやっていただいているので、今はこれ以上期待することは敢えてないと言えますね。顧客情報の管理もきっちりとしていただいているので、安心してお任せできます。</p>

        <p><span class="text-blue">聞き手：</span>大変、身の引き締まる話で(笑)、汗をかいてきました(笑)。<br>今後も更に開発に努めて参りますので、引き続きのご教示をお願いいたします。本日はありがとうございました。</p>

