        <h3>―― 鎌谷さんにとって保全活動とは何でしょうか？若いライフプランナーの方々へのメッセージも含めてお願いします。</h3>
        <p><span class="text-blue">鎌谷：</span>保全というのは、お客様が契約にサインされた瞬間から「相談すれば安心できる」という満足感と、加入した契約に対して「自分に何かがあっても大事な家族が経済的に守られる」安心感を得ていただくことではないでしょうか。そんな形で担当者として何年もお付き合いをするわけですから、担当者の顔も見ないし連絡もないし、何年も会ってないそんな人に聞くのもどうか？と思われないように。「何かがあったら鎌谷に連絡しよう」とか、「あいつに電話したらすぐに来てくれるから」というイメージを常に持ってもらうというのも保全の一つです。<br>普段、物理的に訪問することが無理ならば「何か定期的に彼から連絡がきているな」ということをお客様が感じていただけるようなアクションもまた保全であると思います。<br>そして、契約いただいた時の信頼をできるだけ継続するということ。<br>私達の仕事を評価していただいて、その人の大事な方をご紹介いただく事を想像してみると、「加入した時にいいと思ったけど、加入してからも凄いな！」とご満足していただいている方からのご紹介は濃いですよね。そういう意味では、新規のお客様のマーケティングにつながるのも保全であると言えると思います。<br>例えば、井手上さんが車を買いました。とても対応がよかったので、また誰かいたら紹介するねと言う時の満足感と、購入するときも良かったし、何年か経ってちょっと壊れた時のメンテナンスもすごくいい、待っている時のお店の受付の方の対応もすごくいい、担当セールスマンが精いっぱい対応してくれた。その後の満足感。<br>もし誰かと車の話題になって「今車を考えているんだけど」ともなれば、「良かったら紹介しますよ、買った後のメンテナンスやアフターもいいし」という話になりますよね。買った時よりも買った後の行動が紹介のレベルをよくしてくれると思います。<br>つまり、保全とは課せられた義務ではなく、若い方は次のステップアップのためのアクションであると思います。<br>私みたいにお客様がご高齢になってくると、保全は義務付けられたものではないものの、絶対にしないといけないし早くして差し上げないといけません。と言うとおこがましいですが、早くしないと手遅れになる可能性もあるので、何年も放置するというのは罪です。お客様には「そろそろお伺いしましょうか？」とお知らせしていますが、お客様からのご連絡がもし5年なければお電話をして、「一度内容だけでも確認しましょう」というご連絡を差し上げるようにしています。</p>

        <img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 mb-4 mb-lg-2" src="/images/interview/vol01_img03.jpg" alt="Special interview">

        <p><span class="text-blue">聞き手：</span>保全はキャリアを積んで、時間が経ってからやるのではないということですか？</p>

        <p><span class="text-blue">鎌谷：</span>そう思います。私はご加入された瞬間に保全は始まると思うので、そういう意味では契約をするプロセス、証券のお渡し、その後の個々のいろいろな対応、保険なので例えば前の保険をやめる時のアドバイスであったり、そのあたりぐらいから保全は始まっていると思います。急に何もないのにお電話をするのは変ですし、何もないのにお伺いするのも変なので、いろいろなイベントごとの時に自分を意識してもらえるようなアクションを起こすというのが大事だと思いますね。<br>担当する件数が多くなるとアクションを起こしにくいので顧客数が少ない時から自分に習慣づけしておけばやりやすいと思います。<br>余談ですが、今はパソコンで打てば設計書がすぐに出力できますが、私の時は絵で描いていました（笑）。<br>
            <span class="text-blue">聞き手：</span>鎌谷さんの時代の営業の皆様は、大変なご苦労ですね。</p>

        <p><span class="text-blue">鎌谷：</span>パソコンが支社に2台しかなくて、設計書を出力するためのカラープリンターもなくすべて白黒で、それもライフプランナー全員が設計書をプリントアウトして使える環境ではなかったと思います。それで手書きをするしかない。私もそれでかなり絵を描くのが上手くなったと思います（笑）。</p>
