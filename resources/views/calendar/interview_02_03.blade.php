        <!-- s:interview-box -->
        <section class="interview-box">
          <h3>―― 最後に、私どもに今後期待されること、また、私どものような制作会社に対し、保全活動に関するご希望があれば、お聞かせください。</h3>
          <p><span class="text-blue">中村：</span>現状維持ではなく今までとは違ったもの。そういう創意工夫をしてもらえればありがたいです。何かその年その年で先を見据えた魅力的なカレンダーでそれを通じて何かインスピレーションがインスパイアされて魅了されるようなものを体験、体感させてもらえたらと思います。<br>キューズプラスさんの美しく、普段、目の当たりにすることがない各月のアメージングな写真はお客様の生活の質、向上の後押しをしてくれている感じがします。今までになく他社にもない、キューズプラスさんしかできない価値を引き続き提案していただきたいと思います。</p>

          <p><span class="text-blue">聞き手：</span>他はいかがでしょうか？</p>

          <p><span class="text-blue">中村：</span>何をしているか、より、何をやり続けているかがとても大事ですので、その中で発送代行は本当に助かります。時折返ってくる時がありますが、お引越しされたところに対して郵便局のゆうメールは転送されるので、このサービスはいいですね。またアンケートの挨拶状に関して、保険と旬なトピックスがリンクしているのはとてもいいと思います。露骨に保険セールスにつなげようとしているなとかではなく、さりげなく保険とリンクしているので。<br>あと、要望としては、可能ならカレンダーにプルデンシャルのコーポレートフィロソフィを入れてみてはと思います。「プルデンシャル生命はこういうビジョン、ミッション、ストラテジーがあって、社員は生命保険を通じて社会に貢献しようとしている。我々には時代を開拓する精神、生命保険の正しいあり方を追求する信念がある…」というものです。もちろんお客様には知った事ではないかもしれません。しかし、これをカレンダーに載せるということは常にお客様の目に触れるわけですから、当然、担当のライフプランナーはもちろん会社も襟を正さないといけないし、そんなことを掲げているならそれに合ったサービスをしろと言われるかもしれない。ある意味、退路を断つわけではないですが、それはそれでいいじゃないかと思います。会社も私もそうですが、自分がいままでやってきたことの延長線で今がありますから、自分で思いもつかないような、そういう気づきがあるカレンダーがいいかなと思います。</p>

          <p><span class="text-blue">聞き手：</span>実現できるよう努めていきたいと思います。本日は大変勉強になりました。お話をお聞かせいただき誠にありがとうございました。</p>

