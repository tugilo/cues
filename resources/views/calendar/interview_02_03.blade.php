          <h3>―― 中村さんにとって保全活動とは何でしょうか？</h3>
          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol02_img03.jpg" alt="Special interview">
          <p><span class="text-blue">中村：</span>保全活動とは、ですね？保全とは貢献と信用保持そのものであり、顧客ロイヤリティです。我々の公用語でいうと「ニードセールス」の中の２つめのフェーズにあたります。そしてコミュニケーション。フェイスtoフェイス、ファイナル3フィート。商談の場面で語られることが多いファイナル3フィートですが、保全もまたフェイスtoフェイス、ファイナル3フィート。保険をお預かりした後もコミュニケーションをしっかり取り、フェイスtoフェイス、ファイナル3フィートというお客様との90cmの空間を大切にしていくことではないかと思います。</p>
