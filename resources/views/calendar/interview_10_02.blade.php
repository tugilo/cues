        <section class="interview-box">
          <p><span class="text-blue">聞き手：</span>私どもは池田さんと10年以上お付き合いをさせていただいていますが、毎回お預かりする発送物の部数はほとんど変わらずお任せいただいています。これは本当に凄いなと思っています。</p>

          <p><span class="text-blue">池田：</span>そうですか。ただ、「分け隔てなく平等に」というかたちです。これは個々LPの考え方次第かもしれませんが、要は、「自分にとって何が重要か」という事です。私の場合は、「お客様はお客様」なので、保険契約はその方ご自身が自分で決めて加入したいと思うものだと考えています。したがって、入りたくない人には無理にお勧めするつもりはありません。また、こちらのスタイルというか価値観が合わない方については他社でも良いのでは、という事をはっきりお伝えしています。お客様であるからといって、こちらがへりくだった振る舞いをする必要はありません。むしろ、対等な関係を築く事が重要だと考えています。皆さん平等にお客様になっていただいているので、誰がどうというよりも平等が基本です。こちらからアクションを起こす際には通信物を送る回数や面談の回数を全て統一しています。</p>

          <p><span class="text-blue">聞き手：</span>最初に「平等に関与する」と固くお決めになっていらっしゃるので、ずっと変わりなく続ける事が出来るのですね。</p>

          <p><span class="text-blue">池田：</span>とはいえ、必死ですけどもね。</p>

          <p><span class="text-blue">聞き手：</span>担当LPに対して、お客様が期待される事はどのようなものとお考えでしょうか？もちろん、個人のお客様と法人のお客様とでは違うかもしれませんが。</p>
          
          <p><span class="text-blue">池田：</span>お客様が私に対して期待している事、についてですか？そうですね、お客様からよく言われるのは、「辞めずにいてください」という事でしょうか。それに尽きるのではないでしょうか。やはり担当者が変わるというのはお客さんとってはマイナス要因が大きいのが事実だと思います。それだけに、ずっと同じ担当者でいるというのはお客様にとっても良い事なのではと思います。</p>

          <p><span class="text-blue">聞き手：</span>その期待に応える為のご苦労は、大変なものがありますよね。</p>

          <p><span class="text-blue">池田：</span>その約束を果たす為に必死でやってきた、というイメージです。</p>
        </section>
