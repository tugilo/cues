        <section class="interview-box">
          <h3>―― それでは、保全活動を長年に亘って続けていく為のコツがありましたら教えてください。</h3>

          <img class="img-fluid mx-auto d-block float-lg-right move-lg-right-6 mb-4 mb-lg-2" src="/images/interview/vol10_img02.jpg" alt="Special interview">

          
          <p><span class="text-blue">池田：</span>そうですね。やはりルーティン作業にするのが一番効果的だと思います。特定の月に特定のタスクをこなすという方法が最も簡単です。アシスタントさんに協力してもらってお客様のところで話をしたさまざまな内容をパソコンに入力してもらっています。そして予め決めている「特定の月に対応すべき作業」についてその月になったら私に提示してもらい、私はそれをこなしていく作業フローを作っています。これにより、漏れなく仕事を進める事ができるので、私にとっては最も重要な業務です。実のところ、私はあまり手帳を使わないために、スケジュール管理は主に会社のパソコンを利用しています。例えば、お客様のところに行ってさまざまな話をすると、再来年の特定の月に何をしなければならないといった内容があった場合、それをパソコンに入力し、To Doリストに表示させるような流れを作っています。おそらく、ほとんどの人がそうしていると思いますが。特に現在の若手は皆、それを実施していると思います。というのも、年齢的に私がこの仕事をどれくらい続けられるかを考えた時、私よりも若いお客様については契約が残る可能性があります。したがって、私との間で行われたコミュニケーションや取り決めなどについて一定程度記録しておかないと、私の後輩たちが私のお客様を引き継ぐ際に何も情報がない状態でスタートしなければならず、全くのゼロから始めなければならなくなります。それでは非常に大変です。だからこそ、データをある程度残す事はとても重要だと思っています。</p>
              
              <p><span class="text-blue">聞き手：</span>将来の引継ぎの事を考えたうえで、お客様の状況を常に把握し、更新していく事を習慣としておられるのですね。</p>

              <p><span class="text-blue">池田：</span>そうですね。近年では、ダブル担当制度も少しずつ取り組んでいます。将来、お客様を後輩に引き継ぐ際に何らかの負荷がかかると大変ですし、良好な保全活動を行えませんからね。その観点ではお客様の状況や職業、業種に精通している担当者がいるとベストであるとは思います。しかし、基本的にはお互いに普通に接して、楽しい話題でコミュニケーションが取る事ができればそれで良いと考えています。私のお客様は主にドクターが中心ですが、業種などの知識は後からでも積み重ねられますから、彼らが最初からどこか特定の業界に特化している必要は無いと考えています。</p>
              
              <p><span class="text-blue">聞き手：</span>私はドクターがとても特色があるマーケットであると思っているのですが、向き合う上で何か心がけている事柄はありますか？</p>

              <p><span class="text-blue">池田：</span>よく聞かれる事ですが、私は学校を卒業してから医薬品の卸会社に勤めていたので、社会人になってからは、いわばドクターとしか話をした事がありませんでした。他の一般法人の社長やサラリーマンの方とはあまり接点がなく、ずっとドクターと話す環境に身を置いていましたから、そちらの方が私にとって馴染みやすかったという事です。逆に、一般法人の社長のほうが私は気を遣ってしまいます。私にとってドクターマーケットとは、私自身が気負わずに先生と関係性を築ける場所、というところかと思います。但し、基本的には、例えば先生が往診に行かれる方の場合には診療時間を外して、具体的には午前診が終わって往診に行くまでの時間帯を避けるなど、配慮する事はあります。アポイントを取る際も、どちらかといえば平日は忙しい傾向がある為、土曜日の夜の7時半や8時ころなど、連絡する時間帯に少し気を遣っています。それ以外については特別気を付ける点はないと思います。</p>
