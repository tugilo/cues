        <section class="interview-box">
          <h3>―― 保全活動を継続していくためのコツのようなものがあれば教えてください。</h3>

          <p><span class="text-blue">岡崎：</span>コツは心持ちとして、入社当時から口にしているのは「お客様の親戚ぐらいになりたい」ということです。親戚といえば、毎日顔を合わせるわけではないけど、結婚式にはお祝いを持って参加し、お葬式にはお悔やみに行き、家族を励ます。普段は年に何回か連絡を取り、お互いの近況を確認して安心する。本当にピンチな時は直ぐに駆けつける。こんな感じじゃないでしょうか。それくらいの意識でお客様との距離感が保てたらなと思っています。 それと保全活動は新契約のように必ずしもコミッションが発生するわけではありません。追加契約が預かれなかった時に、その訪問を『失敗』と感じると続かないと思います。私は「お客様が自分の営業マンになってくれたら良いな」というイメージで保全活動を行っています。「久しぶりに岡崎さんが来た。岡崎さんが言っていた話、あの人が面白がるかもしれない」そう思って伝達だけしてくだされば、ありがたい。私には10種類のお金の講座という受け皿がありますから。「自分の営業マンを作っていく」という目的だと失敗という結果はない。だから楽しみながら保全を続けることが出来るのです。</p>
        </section>
        <!-- n:interview-box -->

        <!-- s:interview-box -->
        <section class="interview-box">
          <h3>―― 新規契約の活動が忙しくて保全活動になかなか時間が割けないという事を後輩LPの方から相談を受けた場合、どのようなアドバイスをされるでしょうか？</h3>

          <img class="img-fluid mx-auto d-block float-lg-left move-lg-left-6 mb-4 mb-lg-2" src="/images/interview/vol04_img02.jpg" alt="Special interview">
          <p><span class="text-blue">岡崎：</span>「保全こそ創造力がいる仕事で、保全を制する者はLPとして長く成功すると思うよ」と話をするかもしれませんね。POJには新規のセールスプロセスには選りすぐりのスクリプトやロールプレイ動画が用意されています。一方で保全活動はLPの我流で行われている部分が大きいと感じます。いまでこそSPチームが模範となって保全のロールモデルを作り、会社全体を牽引してくださっています。でもSPに属していないLPは私も含めそのやり方を強制はされていません。だからこそ、自分で工夫して創造力をもって保全活動をすることは、難しくもあり、やりがいもあると思います。私の開催するお客さま向けお金の講座も座談会もこの工夫の一つです。</p>
        </section>
        <!-- n:interview-box -->

        <!-- s:interview-box -->
        <section class="interview-box">
          <h3>―― では、女性からの目線と男性からの目線とで保全活動の内容を比較した場合、考え方や違う点は有るように感じられますか？</h3>

          <p><span class="text-blue">岡崎：</span>保全に限ったことではありませんが、強いて言えばLPとして女性は得だなとは感じます。理由は男性、女性どちらにも共感しやすいという点です。LPをしている以上、いかに仕事を楽しそうにやっていても、事業計画をたて数字を管理し、リスクも負いながら挑戦を続けている。つまりビジネスパーソンとしてファイティングポーズを取り続けている。だから男性が何百万、何千万と稼ぐ苦労もおもんばかれる。一方で自身の家庭では娘、妻、母、姉、妹などそれぞれの顔で家事や家族の世話など細々としたこともこなしているので、女性の大変さが判る。両方の味方になりえるのでお得かなと感じます。</p>
        </section>
